def alias_type_for_db(value: Any) -> str:
    value = normalize_lower(value)
    if value in {"canonical", "surface_variant", "translation", "manual_alias", "synonym", "misspelling"}:
        return value
    if value in {"manual_remap", "manual", "remap"}:
        return "manual_alias"
    if value in {"exact_alias", "surface", "variant"}:
        return "surface_variant"
    return "surface_variant"


def review_status_from_row(row: dict[str, Any]) -> str:
    if safe_bool(row.get("needs_manual_review_v1"), default=False):
        return "pending"
    status = normalize_lower(row.get("eligibility_status_v1"))
    if status == "eligible":
        return "pending"
    if status == "excluded":
        return "rejected"
    return "pending"


def quality_tier_for_db(value: Any) -> str | None:
    value = normalize_lower(value)
    if value in {"high", "medium", "low"}:
        return value
    if value in {"high_quality_signal", "strong_signal"}:
        return "high"
    if value in {"usable_signal", "emerging_signal", "solid_signal"}:
        return "medium"
    if value in {"weak_signal", "single_mention_signal", "noisy_signal"}:
        return "low"
    return None


def evidence_tier_for_db(value: Any) -> str | None:
    value = normalize_lower(value)
    return value if value in VALID_EVIDENCE_TIERS else None


def reliability_from_confidence(value: Any) -> str:
    conf = safe_float(value) or 0.0
    if conf >= 0.75:
        return "high"
    if conf >= 0.50:
        return "medium"
    return "low"


def sentiment_label_for_db(value: Any) -> str | None:
    value = normalize_lower(value)
    return value if value in VALID_SENTIMENT_LABELS else None


def hidden_gem_tier_for_db(value: Any) -> str:
    value = normalize_lower(value)
    return value if value in VALID_HIDDEN_GEM_TIERS else "not_selected"


def ranking_scope_for_db(value: str) -> str:
    value = normalize_lower(value)
    if value not in VALID_DB_RANKING_SCOPES:
        raise ValueError(
            f"Invalid DB ranking scope {value!r}. Current DB constraint normally allows: "
            f"{sorted(VALID_DB_RANKING_SCOPES)}. Use --db-ranking-scope other for sevilla_pilot."
        )
    return value


