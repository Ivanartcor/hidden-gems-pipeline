def reliability_counts_from_signal(row: dict[str, Any]) -> tuple[int, int, int, float | None]:
    mention_count = safe_int(row.get("mention_count")) or 0
    confidence = safe_float(row.get("avg_sentiment_confidence")) or 0.0
    if mention_count <= 0:
        return 0, 0, 0, None
    if confidence >= 0.75:
        return mention_count, 0, 0, 1.0
    if confidence >= 0.50:
        return 0, mention_count, 0, 0.0
    return 0, 0, mention_count, 0.0


