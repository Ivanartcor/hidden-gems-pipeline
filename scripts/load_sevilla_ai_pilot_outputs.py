def get_source_system_id(conn: Connection, schema: str, source_code: str) -> str:
    row = conn.execute(
        text(
            f"""
            SELECT source_system_id
            FROM {qname(schema, 'source_system')}
            WHERE source_code = :source_code
            """
        ),
        {"source_code": source_code},
    ).mappings().first()
    if row is None:
        raise RuntimeError(f"source_system not found for source_code={source_code!r}")
    return str(row["source_system_id"])


