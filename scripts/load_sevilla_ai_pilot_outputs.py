"""Load Sevilla AI pilot artifacts into PostgreSQL.

This script imports the local Sevilla Hidden Gems pilot generated by notebooks 12-17.
It is designed to follow the same flow as the existing AI loaders in the repository,
but adapted to the Spanish/Sevilla prototype artifacts.

It loads, in order:

1. hidden_gems.ai_model_version
2. hidden_gems.ai_pipeline_run
3. hidden_gems.dish
4. hidden_gems.dish_alias
5. hidden_gems.dish_mention
6. hidden_gems.dish_mention_sentiment
7. hidden_gems.dish_place_signal
8. hidden_gems.hidden_gem_candidate

Important note about ranking_scope:
The current 07_ai_module.sql constraint normally allows:
  yelp_prototype, sevilla_global, sevilla_neighborhood, manual_review, other
The notebook artifact uses ranking_scope_v1='sevilla_pilot'. By default this loader stores
that value inside ranking_config_json and uses db_ranking_scope='other' to respect the DB
constraint. If you later update the DB constraint to include 'sevilla_pilot', you can adapt
--db-ranking-scope accordingly.

Recommended dry-run from repository root:

python -m scripts.load_sevilla_ai_pilot_outputs `
  --dry-run

Recommended real load:

python -m scripts.load_sevilla_ai_pilot_outputs `
  --report-path data/artifacts/ai/sevilla/load_sevilla_ai_pilot_outputs_report.json

Optional, if you only want to load selected ranking candidates instead of all scored pairs:

python -m scripts.load_sevilla_ai_pilot_outputs `
  --ranking-path data/artifacts/ai/sevilla/ranking/sevilla_hidden_gems_selected_candidates_v1.jsonl
"""

