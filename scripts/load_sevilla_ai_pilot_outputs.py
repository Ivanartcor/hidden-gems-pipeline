def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Load Sevilla AI pilot outputs into PostgreSQL.")
    parser.add_argument("--catalog-path", type=Path, default=DEFAULT_CATALOG_PATH)
    parser.add_argument("--aliases-path", type=Path, default=DEFAULT_ALIASES_PATH)
    parser.add_argument("--mentions-path", type=Path, default=DEFAULT_MENTIONS_PATH)
    parser.add_argument("--signals-path", type=Path, default=DEFAULT_SIGNALS_PATH)
    parser.add_argument("--ranking-path", type=Path, default=DEFAULT_RANKING_PATH)
    parser.add_argument("--report-path", type=Path, default=DEFAULT_REPORT_PATH)
    parser.add_argument("--schema", default=DEFAULT_SCHEMA)
    parser.add_argument("--source-code", default=DEFAULT_SOURCE_CODE)
    parser.add_argument("--db-ranking-scope", default=DEFAULT_DB_RANKING_SCOPE, choices=sorted(VALID_DB_RANKING_SCOPES))
    parser.add_argument("--artifact-ranking-scope", default=DEFAULT_ARTIFACT_RANKING_SCOPE)
    parser.add_argument("--signal-version", default=DEFAULT_SIGNAL_VERSION)
    parser.add_argument("--ranking-version", default=DEFAULT_RANKING_VERSION)
    parser.add_argument("--max-rows", type=int, default=None)
    parser.add_argument("--dry-run", action="store_true")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    try:
        analyze_or_load(
            catalog_path=args.catalog_path,
            aliases_path=args.aliases_path,
            mentions_path=args.mentions_path,
            signals_path=args.signals_path,
            ranking_path=args.ranking_path,
            report_path=args.report_path,
            schema=args.schema,
            source_code=args.source_code,
            db_ranking_scope=args.db_ranking_scope,
            artifact_ranking_scope=args.artifact_ranking_scope,
            signal_version=args.signal_version,
            ranking_version=args.ranking_version,
            max_rows=args.max_rows,
            dry_run=args.dry_run,
        )
    except Exception as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        raise


if __name__ == "__main__":
    main()
