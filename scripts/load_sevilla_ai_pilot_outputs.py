def read_csv_rows(path: Path, max_rows: int | None = None) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    with path.open("r", encoding="utf-8-sig", newline="") as f:
        reader = csv.DictReader(f)
        for idx, row in enumerate(reader, start=1):
            if max_rows is not None and idx > max_rows:
                break
            rows.append(dict(row))
    return rows


def read_jsonl_rows(path: Path, max_rows: int | None = None) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    with path.open("r", encoding="utf-8") as f:
        for idx, line in enumerate(f, start=1):
            if max_rows is not None and idx > max_rows:
                break
            line = line.strip()
            if not line:
                continue
            rows.append(json.loads(line))
    return rows


def read_artifact_rows(path: Path, max_rows: int | None = None) -> list[dict[str, Any]]:
    if not path.exists():
        raise FileNotFoundError(f"Artifact not found: {path}")
    if path.suffix.lower() == ".jsonl":
        return read_jsonl_rows(path, max_rows=max_rows)
    if path.suffix.lower() == ".csv":
        return read_csv_rows(path, max_rows=max_rows)
    raise ValueError(f"Unsupported artifact format: {path}")


