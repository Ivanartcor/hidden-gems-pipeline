def validate_schema_name(schema: str) -> str:
    schema = str(schema).strip()
    if not ALLOWED_SCHEMA_PATTERN.match(schema):
        raise ValueError(f"Invalid PostgreSQL schema name: {schema!r}")
    return schema


def qname(schema: str, table: str) -> str:
    schema = validate_schema_name(schema)
    return f'"{schema}"."{table}"'


def read_json_file(path: Path | None) -> dict[str, Any]:
    if path is None:
        return {}

    if not path.exists():
        raise FileNotFoundError(f"Summary file not found: {path}")

    with path.open("r", encoding="utf-8") as f:
        data = json.load(f)

    if not isinstance(data, dict):
        raise ValueError(f"Expected a JSON object in {path}")

    return data


def json_dumps(value: Any) -> str:
    return json.dumps(value, ensure_ascii=False, default=str)


def utc_now() -> datetime:
    return datetime.now(timezone.utc)


def normalize_space(value: Any) -> str:
    if value is None or pd.isna(value):
        return ""
    return re.sub(r"\s+", " ", str(value)).strip()


def truncate(value: Any, max_len: int) -> str | None:
    value = normalize_space(value)
    if not value:
        return None
    return value[:max_len]


def none_if_nan(value: Any) -> Any:
    if value is None:
        return None
    try:
        if pd.isna(value):
            return None
    except Exception:
        pass
    return value


def safe_int(value: Any, default: int | None = None) -> int | None:
    value = none_if_nan(value)
    if value is None:
        return default
    try:
        return int(value)
    except Exception:
        return default


def safe_float(value: Any, default: float | None = None) -> float | None:
    value = none_if_nan(value)
    if value is None:
        return default
    try:
        return float(value)
    except Exception:
        return default


def clamp_float(value: Any, min_value: float, max_value: float, default: float | None = None) -> float | None:
    parsed = safe_float(value, default=default)
    if parsed is None:
        return None
    return max(min_value, min(max_value, parsed))


def slugify(value: str, fallback: str = "dish") -> str:
    value = normalize_space(value).lower()
    value = value.replace("&", " and ")
    value = re.sub(r"[^a-z0-9]+", "_", value)
    value = re.sub(r"_+", "_", value).strip("_")
    if not value:
        value = fallback
    if value[0].isdigit():
        value = f"dish_{value}"
    return value[:MAX_VARCHAR_150].strip("_") or fallback


def build_unique_dish_codes(catalog_df: pd.DataFrame) -> dict[str, str]:
    """Return canonical_name -> unique dish_code."""

    used_codes: set[str] = set()
    mapping: dict[str, str] = {}

    for canonical_name in catalog_df["canonical_dish_name_clean"].tolist():
        base_code = slugify(canonical_name)
        code = base_code
        suffix = 2

        while code in used_codes:
            suffix_text = f"_{suffix}"
            code = f"{base_code[:MAX_VARCHAR_150 - len(suffix_text)]}{suffix_text}"
            suffix += 1

        used_codes.add(code)
        mapping[canonical_name] = code

    return mapping


def to_display_name(value: str) -> str:
    value = normalize_space(value)
    if not value:
        return ""
    # .title() is acceptable for this first seed catalog, but keep small words readable.
    return value.title()


def normalize_review_status(value: Any) -> str:
    value = normalize_space(value).lower()
    if value in ALLOWED_DISH_REVIEW_STATUS:
        return value
    if value in {"not_reviewed", "", "none", "nan"}:
        return "pending"
    return "pending"


def normalize_alias_type(value: Any) -> str:
    value = normalize_space(value).lower()
    if value in ALLOWED_ALIAS_TYPES:
        return value
    return "surface_variant"


