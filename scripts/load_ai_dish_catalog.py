def upsert_ai_model_version(conn: Connection, schema: str, definition: dict[str, Any]) -> str:
    sql = text(
        f"""
        INSERT INTO {qname(schema, 'ai_model_version')} (
            model_code,
            model_name,
            model_type,
            task_name,
            version_label,
            description,
            framework_name,
            base_model_name,
            language_scope,
            training_dataset_name,
            training_artifact_path,
            metrics_json,
            config_json,
            is_active
        ) VALUES (
            :model_code,
            :model_name,
            :model_type,
            :task_name,
            :version_label,
            :description,
            :framework_name,
            :base_model_name,
            :language_scope,
            :training_dataset_name,
            :training_artifact_path,
            CAST(:metrics_json AS jsonb),
            CAST(:config_json AS jsonb),
            TRUE
        )
        ON CONFLICT (model_code)
        DO UPDATE SET
            model_name = EXCLUDED.model_name,
            model_type = EXCLUDED.model_type,
            task_name = EXCLUDED.task_name,
            version_label = EXCLUDED.version_label,
            description = EXCLUDED.description,
            framework_name = EXCLUDED.framework_name,
            base_model_name = EXCLUDED.base_model_name,
            language_scope = EXCLUDED.language_scope,
            training_dataset_name = EXCLUDED.training_dataset_name,
            training_artifact_path = EXCLUDED.training_artifact_path,
            metrics_json = EXCLUDED.metrics_json,
            config_json = EXCLUDED.config_json,
            is_active = TRUE
        RETURNING ai_model_version_id::text;
        """
    )

    params = {
        **definition,
        "training_artifact_path": definition.get("training_artifact_path"),
        "metrics_json": json_dumps(definition.get("metrics_json", {})),
        "config_json": json_dumps(definition.get("config_json", {})),
    }

    return str(conn.execute(sql, params).scalar_one())


