def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Load the AI dish catalog and aliases into PostgreSQL."
    )

    parser.add_argument(
        "--catalog-path",
        type=Path,
        required=True,
        help="Path to dish_catalog_seed_v2.csv.",
    )
    parser.add_argument(
        "--aliases-path",
        type=Path,
        required=True,
        help="Path to dish_aliases_seed_v2.csv.",
    )
    parser.add_argument(
        "--summary-path",
        type=Path,
        default=None,
        help="Optional path to dish_normalization_summary_v2.json.",
    )
    parser.add_argument(
        "--schema",
        default=settings.pgschema,
        help=f"Target PostgreSQL schema. Default: {settings.pgschema}.",
    )
    parser.add_argument(
        "--run-code",
        default="ai_run_yelp_dish_normalization_v2_catalog_import",
        help="ai_pipeline_run.run_code to create/update.",
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=None,
        help="Optional row limit for testing.",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Validate inputs and show previews without writing to the database.",
    )

    return parser


def main(argv: list[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)

    try:
        load_ai_dish_catalog(
            catalog_path=args.catalog_path,
            aliases_path=args.aliases_path,
            summary_path=args.summary_path,
            schema=args.schema,
            run_code=args.run_code,
            dry_run=args.dry_run,
            limit=args.limit,
        )
    except Exception as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        return 1

    return 0


