def analyze_or_load(
    *,
    mentions_path: Path,
    schema: str,
    source_code: str,
    mentions_run_code: str,
    sentiment_run_code: str,
    max_rows: int | None,
    strict_business_match: bool,
    dry_run: bool,
) -> dict[str, Any]:
    if not mentions_path.exists():
        raise FileNotFoundError(f"Mentions file not found: {mentions_path}")

    schema = validate_schema_name(schema)

    input_stats = InputStats()
    load_stats = LoadStats(dry_run=dry_run)

    input_artifacts = [
        {
            "artifact_type": "dish_mentions_with_sentiment",
            "path": str(mentions_path),
        }
    ]

    with engine.begin() as conn:
        source_system_id = get_source_system_id(conn, schema, source_code)
        detection_model_version_id = get_model_version_id(conn, schema, DEFAULT_DETECTION_MODEL_CODE)
        sentiment_model_version_id = get_model_version_id(conn, schema, DEFAULT_SENTIMENT_MODEL_CODE)

        review_mapping = load_review_mapping(conn, schema, source_system_id)
        dish_mapping = load_dish_mapping(conn, schema)
        alias_mapping = load_alias_mapping(conn, schema)

        if dry_run:
            mentions_run_id = "DRY_RUN_MENTIONS_RUN_ID"
            sentiment_run_id = "DRY_RUN_SENTIMENT_RUN_ID"
        else:
            mentions_run_id = upsert_ai_pipeline_run(
                conn,
                schema,
                run_code=mentions_run_code,
                run_type="dish_detection",
                input_artifacts=input_artifacts,
                config={
                    "source_code": source_code,
                    "strict_business_match": strict_business_match,
                    "loader": "load_ai_mentions_and_sentiment",
                    "phase": "dish_mention_import",
                },
                notes="Import of dish mentions from Yelp AI prototype artifact. Not Sevilla production output.",
            )
            sentiment_run_id = upsert_ai_pipeline_run(
                conn,
                schema,
                run_code=sentiment_run_code,
                run_type="mention_sentiment",
                input_artifacts=input_artifacts,
                config={
                    "source_code": source_code,
                    "strict_business_match": strict_business_match,
                    "loader": "load_ai_mentions_and_sentiment",
                    "phase": "mention_sentiment_import",
                },
                notes="Import of mention-level sentiment from Yelp AI prototype artifact. Not Sevilla production output.",
            )

        iterator = tqdm(iter_jsonl(mentions_path, max_rows=max_rows), desc="Processing mentions")
        for _, record in iterator:
            input_stats.rows_total += 1

            if record is None:
                input_stats.invalid_json_lines += 1
                continue

            input_stats.rows_considered += 1

            mention_params, sentiment_params, skip_reason = validate_and_prepare_record(
                record,
                review_mapping=review_mapping,
                dish_mapping=dish_mapping,
                alias_mapping=alias_mapping,
                mentions_run_id=mentions_run_id,
                sentiment_run_id=sentiment_run_id,
                detection_model_version_id=detection_model_version_id,
                sentiment_model_version_id=sentiment_model_version_id,
                strict_business_match=strict_business_match,
            )

            if skip_reason is not None:
                if skip_reason == "missing_required_fields":
                    input_stats.skipped_missing_required_fields += 1
                elif skip_reason == "missing_review_mapping":
                    input_stats.skipped_missing_review_mapping += 1
                elif skip_reason == "business_mismatch":
                    input_stats.skipped_business_mismatch += 1
                elif skip_reason == "missing_dish_mapping":
                    input_stats.skipped_missing_dish_mapping += 1
                elif skip_reason == "invalid_sentiment":
                    input_stats.skipped_invalid_sentiment += 1
                continue

            if dry_run:
                load_stats.mentions_upserted += 1
                load_stats.sentiments_upserted += 1
                continue

            assert mention_params is not None
            assert sentiment_params is not None

            dish_mention_id = upsert_dish_mention(conn, schema, mention_params)
            upsert_dish_mention_sentiment(
                conn,
                schema,
                dish_mention_id=dish_mention_id,
                params=sentiment_params,
            )

            load_stats.mentions_upserted += 1
            load_stats.sentiments_upserted += 1

    report = {
        "schema": schema,
        "source_code": source_code,
        "mentions_path": str(mentions_path),
        "max_rows": max_rows,
        "strict_business_match": strict_business_match,
        "dry_run": dry_run,
        "input_stats": asdict(input_stats),
        "load_stats": asdict(load_stats),
        "readiness_after_this_step": {
            "dish_mentions_loaded_or_validated": load_stats.mentions_upserted,
            "dish_mention_sentiments_loaded_or_validated": load_stats.sentiments_upserted,
            "ready_for_signal_loader_if_no_skips": (
                input_stats.skipped_missing_review_mapping == 0
                and input_stats.skipped_business_mismatch == 0
                and input_stats.skipped_missing_dish_mapping == 0
                and input_stats.skipped_invalid_sentiment == 0
            ),
        },
    }

    return report


