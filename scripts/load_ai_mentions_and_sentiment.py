def validate_and_prepare_record(
    record: dict[str, Any],
    *,
    review_mapping: dict[str, dict[str, str]],
    dish_mapping: dict[str, str],
    alias_mapping: dict[tuple[str, str], str],
    mentions_run_id: str,
    sentiment_run_id: str,
    detection_model_version_id: str,
    sentiment_model_version_id: str,
    strict_business_match: bool,
) -> tuple[dict[str, Any] | None, dict[str, Any] | None, str | None]:
    """Return (mention_params, sentiment_params, skip_reason)."""

    source_review_id = normalize_space(record.get("review_id"))
    source_business_id = normalize_space(record.get("business_id"))
    canonical_name = normalize_lower(record.get("canonical_dish_name_v2"))
    mention_text = normalize_space(record.get("dish_mention_text"))
    mention_normalized = normalize_lower(record.get("dish_mention_normalized"))

    if not source_review_id or not source_business_id or not canonical_name or not mention_text:
        return None, None, "missing_required_fields"

    review_info = review_mapping.get(source_review_id)
    if review_info is None:
        return None, None, "missing_review_mapping"

    if strict_business_match and review_info["source_place_record_id"] != source_business_id:
        return None, None, "business_mismatch"

    dish_id = dish_mapping.get(canonical_name)
    if dish_id is None:
        return None, None, "missing_dish_mapping"

    sentiment_label = normalize_lower(record.get("mention_sentiment_label_final"))
    if sentiment_label not in VALID_SENTIMENT_LABELS:
        return None, None, "invalid_sentiment"

    sentiment_method = truncate(record.get("sentiment_method_final"), 100) or "hybrid_context_lexicon_v1_1"
    reliability_tier = normalize_lower(record.get("sentiment_reliability_tier"))
    if reliability_tier not in VALID_RELIABILITY_TIERS:
        reliability_tier = None

    human_review_status = normalize_lower(record.get("human_review_status")) or "not_reviewed"
    if human_review_status not in VALID_HUMAN_REVIEW_STATUS:
        human_review_status = "not_reviewed"

    alias_id = alias_mapping.get((dish_id, mention_normalized))

    mention_params = {
        "review_id": review_info["review_id"],
        "place_id": review_info["place_id"],
        "dish_id": dish_id,
        "dish_alias_id": alias_id,
        "ai_pipeline_run_id": mentions_run_id,
        "model_version_id": detection_model_version_id,
        "mention_text": mention_text,
        "mention_normalized": truncate(mention_normalized, 255),
        "start_char": safe_int(record.get("start_char")),
        "end_char": safe_int(record.get("end_char")),
        "start_token": safe_int(record.get("start_token")),
        "end_token": safe_int(record.get("end_token")),
        "token_count": safe_int(record.get("token_count")),
        "ner_confidence_mean": clamp_float(record.get("confidence_mean"), 0.0, 1.0),
        "ner_confidence_min": clamp_float(record.get("confidence_min"), 0.0, 1.0),
        "ner_confidence_max": clamp_float(record.get("confidence_max"), 0.0, 1.0),
        "normalization_status": truncate(normalize_lower(record.get("normalization_status_v2")) or "unknown", 50),
        "normalization_method": truncate(record.get("normalization_method_v2"), 100),
        "normalization_confidence": None,
        "human_review_status": human_review_status,
    }

    sentiment_params = {
        "ai_pipeline_run_id": sentiment_run_id,
        "model_version_id": sentiment_model_version_id,
        "sentiment_label": sentiment_label,
        "sentiment_score": clamp_float(record.get("mention_sentiment_score_final"), -5.0, 5.0),
        "sentiment_confidence": clamp_float(record.get("mention_sentiment_confidence_final"), 0.0, 1.0),
        "sentiment_reliability_tier": reliability_tier,
        "sentiment_reason": truncate(record.get("sentiment_reason_final"), 100),
        "sentiment_method": sentiment_method,
        "context_sentence": normalize_space(record.get("sentence_context")) or None,
        "context_window": normalize_space(record.get("window_context")) or None,
        "target_clause_context": normalize_space(record.get("target_clause_context_v11")) or None,
        "near_mention_context": normalize_space(record.get("near_mention_context_v11")) or None,
        "positive_terms": json_dumps(parse_list_like(record.get("positive_terms_v11"))),
        "negative_terms": json_dumps(parse_list_like(record.get("negative_terms_v11"))),
        "flags": json_dumps(parse_list_like(record.get("sentiment_flags_final"))),
        "is_training_candidate": safe_bool(record.get("is_sentiment_training_candidate"), default=False),
        "human_review_status": human_review_status,
    }

    return mention_params, sentiment_params, None


