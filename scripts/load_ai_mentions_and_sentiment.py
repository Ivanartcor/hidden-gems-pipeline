def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Load dish mentions and mention sentiment into the Hidden Gems AI schema."
    )
    parser.add_argument(
        "--mentions-path",
        type=Path,
        required=True,
        help="Path to dish_mentions_with_sentiment_hybrid_v1.jsonl.",
    )
    parser.add_argument(
        "--schema",
        default=DEFAULT_SCHEMA,
        help="PostgreSQL schema name. Default: hidden_gems.",
    )
    parser.add_argument(
        "--source-code",
        default=DEFAULT_SOURCE_CODE,
        help="Source system code used to map review.source_review_id. Default: yelp_open_dataset.",
    )
    parser.add_argument(
        "--mentions-run-code",
        default=DEFAULT_MENTIONS_RUN_CODE,
        help="ai_pipeline_run.run_code for dish mention import.",
    )
    parser.add_argument(
        "--sentiment-run-code",
        default=DEFAULT_SENTIMENT_RUN_CODE,
        help="ai_pipeline_run.run_code for mention sentiment import.",
    )
    parser.add_argument(
        "--max-rows",
        type=int,
        default=None,
        help="Optional maximum number of JSONL lines to process.",
    )
    parser.add_argument(
        "--no-strict-business-match",
        action="store_true",
        help="Allow loading a mention even if artifact business_id does not match review.source_place_record_id.",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Validate and count rows without writing to the database.",
    )
    parser.add_argument(
        "--report-path",
        type=Path,
        default=None,
        help="Optional path to save a JSON report.",
    )
    return parser


def main(argv: list[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)

    try:
        report = analyze_or_load(
            mentions_path=args.mentions_path,
            schema=args.schema,
            source_code=args.source_code,
            mentions_run_code=args.mentions_run_code,
            sentiment_run_code=args.sentiment_run_code,
            max_rows=args.max_rows,
            strict_business_match=not args.no_strict_business_match,
            dry_run=args.dry_run,
        )

        print("\n" + "=" * 96)
        print("Load AI mentions and sentiment report")
        print("=" * 96)
        print(json.dumps(report, indent=2, ensure_ascii=False, default=str))

        if args.report_path is not None:
            save_report(args.report_path, report)

        return 0

    except Exception as exc:
        print(f"\nERROR: {exc}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
