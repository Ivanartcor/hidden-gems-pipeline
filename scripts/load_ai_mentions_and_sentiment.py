"""Load dish mentions and mention-level sentiment into PostgreSQL.

This script imports the downstream AI artifact generated by notebook 09:

    dish_mentions_with_sentiment_hybrid_v1.jsonl

It loads two tables:

    hidden_gems.dish_mention
    hidden_gems.dish_mention_sentiment

Prerequisites:

1. The AI schema must exist: db/ddl/07_ai_module.sql.
2. The dish catalog and aliases must already be loaded.
3. Yelp core places and reviews must already be loaded so that:

       artifact review_id  -> hidden_gems.review.source_review_id
       artifact business_id -> hidden_gems.review.source_place_record_id
       artifact business_id -> hidden_gems.place_source_ref.source_record_id

Recommended execution from repository root.

PowerShell dry-run:
    python -m scripts.load_ai_mentions_and_sentiment `
      --mentions-path data/artifacts/ai/sentiment/dish_mentions_with_sentiment_hybrid_v1.jsonl `
      --dry-run

PowerShell real load:
    python -m scripts.load_ai_mentions_and_sentiment `
      --mentions-path data/artifacts/ai/sentiment/dish_mentions_with_sentiment_hybrid_v1.jsonl

Optional report:
    python -m scripts.load_ai_mentions_and_sentiment `
      --mentions-path data/artifacts/ai/sentiment/dish_mentions_with_sentiment_hybrid_v1.jsonl `
      --report-path data/artifacts/ai/sentiment/load_ai_mentions_and_sentiment_report.json

The script is idempotent: mention rows are upserted by
(review_id, start_char, end_char, mention_normalized, ai_pipeline_run_id), and
sentiment rows are upserted by (dish_mention_id, ai_pipeline_run_id, sentiment_method).
"""

