def get_source_system_id(conn: Connection, schema: str, source_code: str) -> str:
    row = conn.execute(
        text(
            f"""
            SELECT source_system_id
            FROM {qname(schema, 'source_system')}
            WHERE source_code = :source_code
            """
        ),
        {"source_code": source_code},
    ).mappings().first()

    if row is None:
        raise RuntimeError(f"source_system not found for source_code={source_code!r}")

    return str(row["source_system_id"])


def get_model_version_id(conn: Connection, schema: str, model_code: str) -> str:
    row = conn.execute(
        text(
            f"""
            SELECT ai_model_version_id
            FROM {qname(schema, 'ai_model_version')}
            WHERE model_code = :model_code
            """
        ),
        {"model_code": model_code},
    ).mappings().first()

    if row is None:
        raise RuntimeError(f"ai_model_version not found for model_code={model_code!r}")

    return str(row["ai_model_version_id"])


def upsert_ai_pipeline_run(
    conn: Connection,
    schema: str,
    *,
    run_code: str,
    run_type: str,
    input_artifacts: list[dict[str, Any]],
    config: dict[str, Any],
    metrics: dict[str, Any] | None = None,
    notes: str | None = None,
) -> str:
    now = utc_now()
    row = conn.execute(
        text(
            f"""
            INSERT INTO {qname(schema, 'ai_pipeline_run')} (
                run_code,
                run_type,
                status,
                started_at,
                finished_at,
                duration_seconds,
                input_artifacts_json,
                output_artifacts_json,
                config_json,
                metrics_json,
                notes
            )
            VALUES (
                :run_code,
                :run_type,
                'completed',
                :started_at,
                :finished_at,
                0,
                CAST(:input_artifacts_json AS jsonb),
                CAST(:output_artifacts_json AS jsonb),
                CAST(:config_json AS jsonb),
                CAST(:metrics_json AS jsonb),
                :notes
            )
            ON CONFLICT (run_code) DO UPDATE SET
                run_type = EXCLUDED.run_type,
                status = EXCLUDED.status,
                finished_at = EXCLUDED.finished_at,
                duration_seconds = EXCLUDED.duration_seconds,
                input_artifacts_json = EXCLUDED.input_artifacts_json,
                config_json = EXCLUDED.config_json,
                metrics_json = EXCLUDED.metrics_json,
                notes = EXCLUDED.notes
            RETURNING ai_pipeline_run_id
            """
        ),
        {
            "run_code": run_code,
            "run_type": run_type,
            "started_at": now,
            "finished_at": now,
            "input_artifacts_json": json_dumps(input_artifacts),
            "output_artifacts_json": json_dumps([]),
            "config_json": json_dumps(config),
            "metrics_json": json_dumps(metrics or {}),
            "notes": notes,
        },
    ).mappings().one()

    return str(row["ai_pipeline_run_id"])


def load_review_mapping(conn: Connection, schema: str, source_system_id: str) -> dict[str, dict[str, str]]:
    print("Loading review mapping from database...")
    rows = conn.execute(
        text(
            f"""
            SELECT
                r.source_review_id,
                r.source_place_record_id,
                r.review_id,
                r.place_id
            FROM {qname(schema, 'review')} r
            WHERE r.source_system_id = :source_system_id
              AND r.source_review_id IS NOT NULL
              AND r.source_place_record_id IS NOT NULL
            """
        ),
        {"source_system_id": source_system_id},
    ).mappings().all()

    mapping: dict[str, dict[str, str]] = {}
    for row in rows:
        mapping[str(row["source_review_id"])] = {
            "review_id": str(row["review_id"]),
            "place_id": str(row["place_id"]),
            "source_place_record_id": str(row["source_place_record_id"]),
        }

    print(f"Review mapping loaded: {len(mapping):,}")
    return mapping


def load_dish_mapping(conn: Connection, schema: str) -> dict[str, str]:
    print("Loading dish mapping from database...")
    rows = conn.execute(
        text(
            f"""
            SELECT dish_id, normalized_name
            FROM {qname(schema, 'dish')}
            WHERE is_active = TRUE
            """
        )
    ).mappings().all()

    mapping = {str(row["normalized_name"]).strip().lower(): str(row["dish_id"]) for row in rows}
    print(f"Dish mapping loaded: {len(mapping):,}")
    return mapping


def load_alias_mapping(conn: Connection, schema: str) -> dict[tuple[str, str], str]:
    print("Loading dish alias mapping from database...")
    rows = conn.execute(
        text(
            f"""
            SELECT dish_alias_id, dish_id, alias_normalized
            FROM {qname(schema, 'dish_alias')}
            WHERE is_active = TRUE
            """
        )
    ).mappings().all()

    mapping: dict[tuple[str, str], str] = {}
    for row in rows:
        key = (str(row["dish_id"]), str(row["alias_normalized"]).strip().lower())
        mapping[key] = str(row["dish_alias_id"])

    print(f"Dish alias mapping loaded: {len(mapping):,}")
    return mapping


