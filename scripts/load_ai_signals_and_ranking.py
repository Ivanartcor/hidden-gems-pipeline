def utc_now() -> datetime:
    return datetime.now(timezone.utc)


def validate_schema_name(schema: str) -> str:
    schema = str(schema).strip()
    if not SCHEMA_RE.match(schema):
        raise ValueError(f"Invalid PostgreSQL schema name: {schema!r}")
    return schema


def qname(schema: str, table: str) -> str:
    schema = validate_schema_name(schema)
    return f'"{schema}"."{table}"'


def normalize_space(value: Any) -> str:
    if value is None:
        return ""
    if isinstance(value, float) and pd.isna(value):
        return ""
    return re.sub(r"\s+", " ", str(value)).strip()


def normalize_lower(value: Any) -> str:
    return normalize_space(value).lower()


def truncate(value: Any, max_len: int) -> str | None:
    value = normalize_space(value)
    if not value:
        return None
    return value[:max_len]


def safe_float(value: Any) -> float | None:
    if value is None:
        return None
    if isinstance(value, float) and pd.isna(value):
        return None
    try:
        return float(value)
    except Exception:
        return None


def safe_int(value: Any) -> int | None:
    value_float = safe_float(value)
    if value_float is None:
        return None
    try:
        return int(value_float)
    except Exception:
        return None


def safe_bool(value: Any, default: bool = False) -> bool:
    if isinstance(value, bool):
        return value
    if value is None:
        return default
    if isinstance(value, float) and pd.isna(value):
        return default
    value_str = str(value).strip().lower()
    if value_str in {"true", "1", "yes", "y", "t"}:
        return True
    if value_str in {"false", "0", "no", "n", "f"}:
        return False
    return default


def clamp_float(value: Any, min_value: float, max_value: float) -> float | None:
    value_float = safe_float(value)
    if value_float is None:
        return None
    return max(min_value, min(max_value, value_float))


def non_negative_int(value: Any, default: int = 0) -> int:
    value_int = safe_int(value)
    if value_int is None:
        return default
    return max(0, value_int)


def ratio_value(value: Any) -> float | None:
    return clamp_float(value, 0.0, 1.0)


def score_minus_one_to_one(value: Any) -> float | None:
    return clamp_float(value, -1.0, 1.0)


def score_zero_to_hundred(value: Any) -> float | None:
    return clamp_float(value, 0.0, 100.0)


def parse_list_like(value: Any) -> list[str]:
    if value is None:
        return []
    if isinstance(value, float) and pd.isna(value):
        return []
    if isinstance(value, list):
        return [normalize_space(v) for v in value if normalize_space(v)]
    if isinstance(value, tuple):
        return [normalize_space(v) for v in value if normalize_space(v)]

    value_str = str(value).strip()
    if not value_str:
        return []

    try:
        parsed = json.loads(value_str)
        if isinstance(parsed, list):
            return [normalize_space(v) for v in parsed if normalize_space(v)]
    except Exception:
        pass

    try:
        parsed = ast.literal_eval(value_str)
        if isinstance(parsed, list):
            return [normalize_space(v) for v in parsed if normalize_space(v)]
    except Exception:
        pass

    if value_str.startswith("[") and value_str.endswith("]"):
        inner = value_str[1:-1].strip()
        if not inner:
            return []
        return [part.strip().strip("'").strip('"') for part in inner.split(",") if part.strip()]

    return [value_str]


def json_dumps(value: Any) -> str:
    return json.dumps(value, ensure_ascii=False, default=str)


def read_csv_records(path: Path, max_rows: int | None = None) -> tuple[list[dict[str, Any]], list[str]]:
    if not path.exists():
        raise FileNotFoundError(f"File not found: {path}")

    df = pd.read_csv(path, nrows=max_rows, keep_default_na=False)
    return df.to_dict(orient="records"), df.columns.tolist()


def write_json_report(path: Path, report: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        json.dump(report, f, indent=2, ensure_ascii=False, default=str)
    print(f"Report written to: {path}")


