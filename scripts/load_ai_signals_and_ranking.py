def run_loader(args: argparse.Namespace) -> dict[str, Any]:
    schema = validate_schema_name(args.schema)
    source_code = args.source_code

    if args.ranking_scope not in VALID_RANKING_SCOPES:
        raise ValueError(f"Invalid ranking scope: {args.ranking_scope}")

    business_signals_records: list[dict[str, Any]] = []
    ranking_records: list[dict[str, Any]] = []
    business_signals_columns: list[str] = []
    ranking_columns: list[str] = []

    if args.business_signals_path:
        business_signals_records, business_signals_columns = read_csv_records(
            Path(args.business_signals_path),
            max_rows=args.max_signal_rows,
        )

    if args.ranking_path:
        ranking_records, ranking_columns = read_csv_records(
            Path(args.ranking_path),
            max_rows=args.max_ranking_rows,
        )

    signal_stats = SignalInputStats(
        rows_total=len(business_signals_records),
        rows_considered=len(business_signals_records),
    )
    ranking_stats = RankingInputStats(
        rows_total=len(ranking_records),
        rows_considered=len(ranking_records),
    )
    load_stats = LoadStats(dry_run=args.dry_run)

    with engine.connect() as conn:
        tx = conn.begin()

        try:
            source_system_id = get_source_system_id(conn, schema, source_code)
            place_mapping = load_place_mapping(conn, schema, source_system_id)
            dish_mapping = load_dish_mapping(conn, schema)

            signal_run_id = "00000000-0000-0000-0000-000000000000"
            ranking_run_id = "00000000-0000-0000-0000-000000000000"

            if not args.dry_run:
                signal_run_id = upsert_ai_pipeline_run(
                    conn,
                    schema,
                    run_code=args.signal_run_code,
                    run_type="signal_aggregation",
                    input_artifacts=[{"path": str(args.business_signals_path), "kind": "dish_business_ranking_candidates"}],
                    output_artifacts=[{"table": f"{schema}.dish_place_signal"}],
                    config={
                        "signal_version": args.signal_version,
                        "source_code": source_code,
                        "ranking_scope": args.ranking_scope,
                    },
                    metrics={"input_rows": len(business_signals_records)},
                    notes="Import of Yelp prototype dish-place signals generated by notebook 10. Not production Sevilla data.",
                )

                ranking_run_id = upsert_ai_pipeline_run(
                    conn,
                    schema,
                    run_code=args.ranking_run_code,
                    run_type="hidden_gems_ranking",
                    input_artifacts=[{"path": str(args.ranking_path), "kind": "hidden_gems_selected_candidates"}],
                    output_artifacts=[{"table": f"{schema}.hidden_gem_candidate"}],
                    config={
                        "ranking_version": args.ranking_version,
                        "ranking_scope": args.ranking_scope,
                        "source_code": source_code,
                    },
                    metrics={"input_rows": len(ranking_records)},
                    notes="Import of Yelp prototype Hidden Gems ranking generated by notebook 11. Not production Sevilla data.",
                )

            signal_mapping: dict[tuple[str, str], str] = {}

            if business_signals_records:
                print("Preparing/loading dish_place_signal rows...")
                for row in tqdm(business_signals_records, desc="Signals"):
                    params, skip_reason = validate_and_prepare_signal(
                        row,
                        place_mapping=place_mapping,
                        dish_mapping=dish_mapping,
                        signal_run_id=signal_run_id,
                        signal_version=args.signal_version,
                    )

                    if skip_reason:
                        if skip_reason == "missing_required_fields":
                            signal_stats.skipped_missing_required_fields += 1
                        elif skip_reason == "missing_place_mapping":
                            signal_stats.skipped_missing_place_mapping += 1
                        elif skip_reason == "missing_dish_mapping":
                            signal_stats.skipped_missing_dish_mapping += 1
                        elif skip_reason == "invalid_counts":
                            signal_stats.skipped_invalid_counts += 1
                        elif skip_reason == "invalid_tiers":
                            signal_stats.skipped_invalid_tiers += 1
                        continue

                    assert params is not None

                    if args.dry_run:
                        load_stats.signals_upserted += 1
                        signal_mapping[(params["place_id"], params["dish_id"])] = "dry-run-signal-id"
                    else:
                        signal_id = upsert_dish_place_signal(conn, schema, params)
                        load_stats.signals_upserted += 1
                        signal_mapping[(params["place_id"], params["dish_id"])] = signal_id

            elif not args.dry_run:
                signal_mapping = load_existing_signal_mapping(
                    conn,
                    schema,
                    ai_pipeline_run_id=signal_run_id,
                    signal_version=args.signal_version,
                )

            if ranking_records:
                print("Preparing/loading hidden_gem_candidate rows...")
                for row in tqdm(ranking_records, desc="Ranking"):
                    params, skip_reason = validate_and_prepare_ranking(
                        row,
                        place_mapping=place_mapping,
                        dish_mapping=dish_mapping,
                        signal_mapping=signal_mapping,
                        ranking_run_id=ranking_run_id,
                        ranking_version=args.ranking_version,
                        ranking_scope=args.ranking_scope,
                    )

                    if skip_reason:
                        if skip_reason == "missing_required_fields":
                            ranking_stats.skipped_missing_required_fields += 1
                        elif skip_reason == "missing_place_mapping":
                            ranking_stats.skipped_missing_place_mapping += 1
                        elif skip_reason == "missing_dish_mapping":
                            ranking_stats.skipped_missing_dish_mapping += 1
                        elif skip_reason == "missing_signal_mapping":
                            ranking_stats.skipped_missing_signal_mapping += 1
                        elif skip_reason == "invalid_score":
                            ranking_stats.skipped_invalid_score += 1
                        elif skip_reason == "invalid_tier":
                            ranking_stats.skipped_invalid_tier += 1
                        continue

                    assert params is not None

                    if args.dry_run:
                        load_stats.hidden_gem_candidates_upserted += 1
                    else:
                        upsert_hidden_gem_candidate(conn, schema, params)
                        load_stats.hidden_gem_candidates_upserted += 1

            if args.dry_run:
                tx.rollback()
            else:
                tx.commit()

        except Exception:
            tx.rollback()
            raise

    report = {
        "schema": schema,
        "source_code": source_code,
        "business_signals_path": str(args.business_signals_path) if args.business_signals_path else None,
        "ranking_path": str(args.ranking_path) if args.ranking_path else None,
        "signal_version": args.signal_version,
        "ranking_version": args.ranking_version,
        "ranking_scope": args.ranking_scope,
        "dry_run": args.dry_run,
        "columns_detected": {
            "business_signals": business_signals_columns,
            "ranking": ranking_columns,
        },
        "input_stats": {
            "signals": asdict(signal_stats),
            "ranking": asdict(ranking_stats),
        },
        "load_stats": asdict(load_stats),
        "readiness_after_this_step": {
            "dish_place_signals_loaded_or_validated": load_stats.signals_upserted,
            "hidden_gem_candidates_loaded_or_validated": load_stats.hidden_gem_candidates_upserted,
            "ready_for_querying_ai_ranking": (
                load_stats.signals_upserted > 0
                and load_stats.hidden_gem_candidates_upserted > 0
                and signal_stats.skipped_missing_place_mapping == 0
                and signal_stats.skipped_missing_dish_mapping == 0
                and ranking_stats.skipped_missing_signal_mapping == 0
                and ranking_stats.skipped_missing_place_mapping == 0
                and ranking_stats.skipped_missing_dish_mapping == 0
            ),
        },
    }

    return report


