def build_arg_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Load AI dish-place signals and Hidden Gems ranking artifacts into PostgreSQL."
    )

    parser.add_argument("--business-signals-path", type=Path, required=True)
    parser.add_argument("--ranking-path", type=Path, required=True)
    parser.add_argument("--schema", default=DEFAULT_SCHEMA)
    parser.add_argument("--source-code", default=DEFAULT_SOURCE_CODE)
    parser.add_argument("--signal-run-code", default=DEFAULT_SIGNAL_RUN_CODE)
    parser.add_argument("--ranking-run-code", default=DEFAULT_RANKING_RUN_CODE)
    parser.add_argument("--signal-version", default=DEFAULT_SIGNAL_VERSION)
    parser.add_argument("--ranking-version", default=DEFAULT_RANKING_VERSION)
    parser.add_argument("--ranking-scope", default=DEFAULT_RANKING_SCOPE)
    parser.add_argument("--max-signal-rows", type=int, default=None)
    parser.add_argument("--max-ranking-rows", type=int, default=None)
    parser.add_argument("--report-path", type=Path, default=None)
    parser.add_argument("--dry-run", action="store_true")

    return parser


def main() -> None:
    parser = build_arg_parser()
    args = parser.parse_args()

    report = run_loader(args)

    print("\n" + "=" * 96)
    print("AI signals and ranking load summary")
    print("=" * 96)
    print(json.dumps(report, indent=2, ensure_ascii=False, default=str)[:12000])

    if args.report_path:
        write_json_report(args.report_path, report)


if __name__ == "__main__":
    main()
