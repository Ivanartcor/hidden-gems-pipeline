def build_signal_quality_flags(row: dict[str, Any]) -> list[str]:
    flags = parse_list_like(row.get("dish_name_quality_flags"))

    if safe_bool(row.get("is_hard_noise_dish_name"), default=False):
        flags.append("hard_noise_dish_name")

    if safe_bool(row.get("is_possible_beverage"), default=False):
        flags.append("possible_beverage")

    if not safe_bool(row.get("is_ranking_ready_v1"), default=False):
        flags.append("not_ranking_ready_v1")

    candidate_tier = normalize_lower(row.get("preliminary_candidate_tier_v1"))
    if candidate_tier:
        flags.append(f"candidate_tier:{candidate_tier}")

    return sorted(set(flag for flag in flags if flag))


def build_ranking_quality_flags(row: dict[str, Any]) -> list[str]:
    flags = parse_list_like(row.get("dish_name_quality_flags"))

    if safe_bool(row.get("is_hard_noise_dish_name"), default=False):
        flags.append("hard_noise_dish_name")

    if safe_bool(row.get("is_possible_beverage"), default=False):
        flags.append("possible_beverage")

    tier = normalize_lower(row.get("hidden_gem_tier_v1"))
    if tier:
        flags.append(f"hidden_gem_tier:{tier}")

    return sorted(set(flag for flag in flags if flag))


def validate_and_prepare_signal(
    row: dict[str, Any],
    *,
    place_mapping: dict[str, str],
    dish_mapping: dict[str, str],
    signal_run_id: str,
    signal_version: str,
) -> tuple[dict[str, Any] | None, str | None]:
    business_id = normalize_space(row.get("business_id"))
    canonical_name = normalize_lower(row.get("canonical_dish_name_v2"))

    if not business_id or not canonical_name:
        return None, "missing_required_fields"

    place_id = place_mapping.get(business_id)
    if place_id is None:
        return None, "missing_place_mapping"

    dish_id = dish_mapping.get(canonical_name)
    if dish_id is None:
        return None, "missing_dish_mapping"

    mention_count = non_negative_int(row.get("mention_count"))
    review_count = non_negative_int(row.get("review_count"))
    positive_mentions = non_negative_int(row.get("positive_mentions"))
    neutral_mentions = non_negative_int(row.get("neutral_mentions"))
    negative_mentions = non_negative_int(row.get("negative_mentions"))

    if positive_mentions + neutral_mentions + negative_mentions > mention_count:
        return None, "invalid_counts"

    high_rel = non_negative_int(row.get("high_reliability_mentions"))
    medium_rel = non_negative_int(row.get("medium_reliability_mentions"))
    low_rel = non_negative_int(row.get("low_reliability_mentions"))

    if high_rel + medium_rel + low_rel > mention_count:
        return None, "invalid_counts"

    aggregate_quality_tier = normalize_lower(row.get("aggregate_quality_tier")) or None
    if aggregate_quality_tier not in VALID_AGGREGATE_QUALITY_TIERS:
        aggregate_quality_tier = None

    evidence_tier = normalize_lower(row.get("business_dish_evidence_tier")) or None
    if evidence_tier not in VALID_EVIDENCE_TIERS:
        return None, "invalid_tiers"

    params = {
        "place_id": place_id,
        "dish_id": dish_id,
        "ai_pipeline_run_id": signal_run_id,
        "signal_version": signal_version,
        "mention_count": mention_count,
        "review_count": review_count,
        "positive_mentions": positive_mentions,
        "neutral_mentions": neutral_mentions,
        "negative_mentions": negative_mentions,
        "positive_ratio": ratio_value(row.get("positive_ratio")),
        "negative_ratio": ratio_value(row.get("negative_ratio")),
        "avg_ner_confidence": ratio_value(row.get("avg_ner_confidence")),
        "avg_sentiment_confidence": ratio_value(row.get("avg_sentiment_confidence")),
        "avg_signal_weight": safe_float(row.get("avg_signal_weight")),
        "total_signal_weight": safe_float(row.get("total_signal_weight")),
        "avg_rating": clamp_float(row.get("avg_rating"), 0.0, 5.0),
        "avg_sentiment_score_raw": clamp_float(row.get("avg_sentiment_score_raw"), -5.0, 5.0),
        "confidence_weighted_sentiment": score_minus_one_to_one(row.get("confidence_weighted_sentiment")),
        "bayesian_sentiment_score": score_minus_one_to_one(row.get("bayesian_sentiment_score")),
        "reliability_high_ratio": ratio_value(row.get("reliability_high_ratio")),
        "high_reliability_mentions": high_rel,
        "medium_reliability_mentions": medium_rel,
        "low_reliability_mentions": low_rel,
        "aggregate_quality_tier": aggregate_quality_tier,
        "evidence_tier": evidence_tier,
        "is_rankable_candidate": safe_bool(row.get("is_ranking_ready_v1"), default=False),
        "quality_flags": json_dumps(build_signal_quality_flags(row)),
    }

    return params, None


def validate_and_prepare_ranking(
    row: dict[str, Any],
    *,
    place_mapping: dict[str, str],
    dish_mapping: dict[str, str],
    signal_mapping: dict[tuple[str, str], str],
    ranking_run_id: str,
    ranking_version: str,
    ranking_scope: str,
) -> tuple[dict[str, Any] | None, str | None]:
    business_id = normalize_space(row.get("business_id"))
    canonical_name = normalize_lower(row.get("canonical_dish_name_v2"))

    if not business_id or not canonical_name:
        return None, "missing_required_fields"

    place_id = place_mapping.get(business_id)
    if place_id is None:
        return None, "missing_place_mapping"

    dish_id = dish_mapping.get(canonical_name)
    if dish_id is None:
        return None, "missing_dish_mapping"

    signal_id = signal_mapping.get((place_id, dish_id))
    if signal_id is None:
        return None, "missing_signal_mapping"

    hidden_gem_score = score_zero_to_hundred(row.get("hidden_gem_score_v1"))
    if hidden_gem_score is None:
        return None, "invalid_score"

    hidden_gem_tier = normalize_lower(row.get("hidden_gem_tier_v1"))
    if hidden_gem_tier not in VALID_HIDDEN_GEM_TIERS:
        return None, "invalid_tier"

    is_selected = safe_bool(row.get("is_hidden_gem_candidate_v1"), default=(hidden_gem_tier != "not_selected"))
    selected_rank = safe_int(row.get("hidden_gem_selected_rank_v1"))
    if is_selected and selected_rank is None:
        # Constraint requires selected rank when selected.
        selected_rank = safe_int(row.get("hidden_gem_rank_v1")) or 1

    params = {
        "place_id": place_id,
        "dish_id": dish_id,
        "dish_place_signal_id": signal_id,
        "neighborhood_id": None,
        "district_id": None,
        "ai_pipeline_run_id": ranking_run_id,
        "ranking_version": ranking_version,
        "ranking_scope": ranking_scope,
        "hidden_gem_score": hidden_gem_score,
        "hidden_gem_score_base": score_zero_to_hundred(row.get("hidden_gem_score_base_v1")),
        "hidden_gem_rank": safe_int(row.get("hidden_gem_rank_v1")),
        "hidden_gem_selected_rank": selected_rank,
        "hidden_gem_tier": hidden_gem_tier,
        "is_selected": is_selected,
        "local_sentiment_component": ratio_value(row.get("local_sentiment_component")),
        "evidence_component": ratio_value(row.get("evidence_component")),
        "confidence_component": ratio_value(row.get("confidence_component")),
        "positive_balance_component": ratio_value(row.get("positive_balance_component")),
        "rarity_component": ratio_value(row.get("rarity_component")),
        "local_outperformance_component": ratio_value(row.get("local_outperformance_component")),
        "hiddenness_component": ratio_value(row.get("hiddenness_component")),
        "preliminary_component": ratio_value(row.get("preliminary_component")),
        "negative_penalty_factor": ratio_value(row.get("negative_penalty_factor_final")),
        "beverage_penalty_factor": ratio_value(row.get("beverage_penalty_factor_final")),
        "noise_penalty_factor": ratio_value(row.get("noise_penalty_factor_final")),
        "low_evidence_penalty_factor": ratio_value(row.get("low_evidence_penalty_factor_final")),
        "ranking_explanation": normalize_space(row.get("ranking_explanation_v1")) or None,
        "ranking_config_json": json_dumps({
            "ranking_version": ranking_version,
            "ranking_scope": ranking_scope,
            "source": "notebook_11_hidden_gems_ranking_v1",
            "business_id": business_id,
            "canonical_dish_name_v2": canonical_name,
        }),
        "quality_flags": json_dumps(build_ranking_quality_flags(row)),
        "is_production_ready": False,
        "human_review_status": "not_reviewed",
    }

    return params, None


