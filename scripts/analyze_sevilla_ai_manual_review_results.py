def load_reviewed_sample(input_path: Path) -> pd.DataFrame:
    if not input_path.exists():
        raise FileNotFoundError(f"Input file not found: {input_path}")

    df = pd.read_csv(input_path)
    if df.empty:
        raise ValueError("Input sample is empty.")

    required = ["hidden_gem_candidate_id", "hidden_gem_tier", "hidden_gem_score", "manual_decision", "manual_error_type"]
    missing = [c for c in required if c not in df.columns]
    if missing:
        raise KeyError(f"Missing required columns: {missing}")

    for col in MANUAL_COLUMNS:
        if col not in df.columns:
            df[col] = ""

    for col in ["manual_decision", "manual_error_type", "manual_should_keep_for_v2"]:
        default = "missing_review" if col in ["manual_decision", "manual_error_type"] else "unclear"
        df[col] = normalize_str_series(df[col], default=default)

    yes_no_cols = [
        "manual_dish_is_correct",
        "manual_place_dish_association_is_correct",
        "manual_sentiment_is_correct",
        "manual_evidence_is_sufficient",
        "manual_ranking_position_is_reasonable",
    ]
    for col in yes_no_cols:
        df[col] = normalize_str_series(df[col], default="unclear").str.lower()

    df["manual_should_keep_for_v2"] = df["manual_should_keep_for_v2"].str.lower()

    numeric_cols = [
        "hidden_gem_selected_rank",
        "hidden_gem_rank",
        "hidden_gem_score",
        "mention_count",
        "review_count",
        "positive_mentions",
        "neutral_mentions",
        "negative_mentions",
        "positive_ratio",
        "negative_ratio",
        "avg_sentiment_confidence",
        "avg_rating",
        "total_signal_weight",
    ]
    df = safe_numeric(df, numeric_cols)

    if "is_selected" in df.columns:
        df["is_selected_bool"] = as_bool_like(df["is_selected"])
    else:
        df["is_selected_bool"] = df["hidden_gem_tier"] != "not_selected"

    for flag in ["generic_dish_flag", "low_evidence_flag", "possible_overranking_flag", "possible_underranking_flag"]:
        if flag in df.columns:
            df[f"{flag}_bool"] = as_bool_like(df[flag])
        else:
            df[f"{flag}_bool"] = False

    df["tier_order"] = df["hidden_gem_tier"].map(TIER_ORDER).fillna(-1).astype(int)
    if "evidence_tier" in df.columns:
        df["evidence_order"] = df["evidence_tier"].map(EVIDENCE_ORDER).fillna(0).astype(int)

    return df


def analyze_mentions(mentions_path: Path | None, sample_ids: set[str]) -> tuple[pd.DataFrame, dict[str, Any]]:
    if not mentions_path or not mentions_path.exists():
        return pd.DataFrame(), {
            "mentions_path_exists": False,
            "mention_rows": 0,
            "linked_to_sample": None,
            "exact_duplicate_rows": 0,
            "candidate_review_duplicate_pairs": 0,
        }

    mentions = pd.read_csv(mentions_path)
    if mentions.empty:
        return mentions, {
            "mentions_path_exists": True,
            "mention_rows": 0,
            "linked_to_sample": True,
            "exact_duplicate_rows": 0,
            "candidate_review_duplicate_pairs": 0,
        }

    id_col = "hidden_gem_candidate_id"
    linked = bool(id_col in mentions.columns and set(mentions[id_col].astype(str).dropna()).issubset(sample_ids))

    exact_cols = [c for c in [id_col, "review_id", "dish_id", "mention_text", "context_sentence"] if c in mentions.columns]
    exact_duplicate_rows = int(mentions.duplicated(subset=exact_cols, keep=False).sum()) if exact_cols else 0

    pair_cols = [c for c in [id_col, "review_id"] if c in mentions.columns]
    candidate_review_duplicate_pairs = 0
    if pair_cols:
        candidate_review_duplicate_pairs = int(mentions.duplicated(subset=pair_cols, keep=False).sum())

    sentiment_distribution = {}
    if "sentiment_label" in mentions.columns:
        sentiment_distribution = mentions["sentiment_label"].fillna("missing").astype(str).value_counts().to_dict()

    confidence_summary = {}
    if "sentiment_confidence" in mentions.columns:
        conf = pd.to_numeric(mentions["sentiment_confidence"], errors="coerce")
        confidence_summary = conf.describe().round(4).to_dict()

    summary = {
        "mentions_path_exists": True,
        "mention_rows": int(len(mentions)),
        "linked_to_sample": linked,
        "unique_candidates_with_mentions": int(mentions[id_col].nunique()) if id_col in mentions.columns else None,
        "exact_duplicate_rows": exact_duplicate_rows,
        "candidate_review_duplicate_pairs": candidate_review_duplicate_pairs,
        "sentiment_distribution": sentiment_distribution,
        "sentiment_confidence_summary": confidence_summary,
        "has_full_review_text": bool("review_text_raw" in mentions.columns),
    }
    return mentions, summary


def build_action_plan(df: pd.DataFrame, mention_summary: dict[str, Any]) -> list[dict[str, Any]]:
    total = len(df)
    if total == 0:
        return []

    error_counts = df["manual_error_type"].value_counts().to_dict()
    decision_counts = df["manual_decision"].value_counts().to_dict()

    actions: list[dict[str, Any]] = []

    evidence_count = int(error_counts.get("evidence_too_weak", 0) + decision_counts.get("needs_more_evidence", 0))
    evidence_rate = evidence_count / total
    if evidence_rate >= 0.25:
        actions.append(
            {
                "priority": 1,
                "area": "evidence_and_tiering",
                "finding": "La principal fuente de revisión es evidencia insuficiente o baja confianza.",
                "recommended_action": "Crear ranking v2 con penalización más explícita por baja evidencia y requisitos mínimos diferenciados por tier.",
                "implementation_hint": "top/strong deberían exigir más reviews o evidencia strong/solid; exploratory puede aceptar emerging pero claramente marcado.",
                "supporting_count": evidence_count,
                "supporting_rate": round(evidence_rate * 100, 2),
            }
        )

    norm_count = int(error_counts.get("dish_normalization_error", 0) + error_counts.get("dish_detection_error", 0))
    if norm_count >= 3:
        actions.append(
            {
                "priority": 2,
                "area": "dish_detection_and_normalization",
                "finding": "Hay errores o dudas de normalización/detección de platos.",
                "recommended_action": "Ampliar lexicón gastronómico español, revisar aliases y añadir reglas para platos compuestos.",
                "implementation_hint": "Priorizar platos locales: solomillo al whisky, carrillada, montadito de pringá, ensaladilla, torrija, croquetas, tortilla de patatas.",
                "supporting_count": norm_count,
                "supporting_rate": round(norm_count / total * 100, 2),
            }
        )

    sentiment_count = int(error_counts.get("sentiment_error", 0))
    if sentiment_count >= 3:
        actions.append(
            {
                "priority": 3,
                "area": "mention_sentiment",
                "finding": "El sentimiento por mención tiene casos problemáticos o demasiado dependientes del rating/contexto general.",
                "recommended_action": "Refinar reglas de sentimiento contextual y separar mejor sentimiento del plato frente a sentimiento general del local.",
                "implementation_hint": "Aumentar peso de contexto local cercano al plato y reducir influencia de rating cuando el texto no valora explícitamente el plato.",
                "supporting_count": sentiment_count,
                "supporting_rate": round(sentiment_count / total * 100, 2),
            }
        )

    generic_count = int(error_counts.get("generic_dish_too_broad", 0) + df.get("generic_dish_flag_bool", pd.Series(dtype=bool)).sum())
    if generic_count >= 3:
        actions.append(
            {
                "priority": 4,
                "area": "generic_dish_control",
                "finding": "Algunos platos genéricos pueden quedar demasiado favorecidos.",
                "recommended_action": "Añadir componente de especificidad o mayor umbral de evidencia para platos genéricos.",
                "implementation_hint": "No eliminar pizza/hamburguesa, pero exigir más evidencia o outperformance para tiers altos.",
                "supporting_count": generic_count,
                "supporting_rate": round(generic_count / total * 100, 2),
            }
        )

    rank_issue_count = int(
        error_counts.get("ranking_score_issue", 0)
        + decision_counts.get("should_rank_higher", 0)
        + decision_counts.get("should_rank_lower", 0)
    )
    if rank_issue_count >= 3:
        actions.append(
            {
                "priority": 5,
                "area": "ranking_formula_v2",
                "finding": "Existen candidatos que deberían subir o bajar respecto al ranking v1.",
                "recommended_action": "Crear ranking v2 y comparar v1 vs v2 antes de cargar nuevos resultados.",
                "implementation_hint": "Ajustar pesos de evidence, specificity, hiddenness, low_evidence_penalty y generic_dish_penalty.",
                "supporting_count": rank_issue_count,
                "supporting_rate": round(rank_issue_count / total * 100, 2),
            }
        )

    if mention_summary.get("exact_duplicate_rows", 0) > 0:
        actions.append(
            {
                "priority": 6,
                "area": "mention_deduplication",
                "finding": "Se han detectado ejemplos de menciones duplicadas exactas en el archivo de menciones.",
                "recommended_action": "Añadir deduplicación exacta en exports y revisar si el origen de duplicados está en dish_mention.",
                "implementation_hint": "Deduplicar por candidate_id + review_id + dish_id + mention_text + context_sentence; revisar start_char/end_char si están disponibles.",
                "supporting_count": int(mention_summary.get("exact_duplicate_rows", 0)),
                "supporting_rate": None,
            }
        )

    return sorted(actions, key=lambda x: x["priority"])


