def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Analyze manual review results for Sevilla Hidden Gems IA v2 planning.")
    parser.add_argument("--input-path", type=Path, default=DEFAULT_INPUT_PATH)
    parser.add_argument("--mentions-path", type=Path, default=DEFAULT_MENTIONS_PATH)
    parser.add_argument("--output-dir", type=Path, default=DEFAULT_OUTPUT_DIR)
    parser.add_argument("--strict", action="store_true", help="Return a non-zero exit code if critical checks fail.")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    output_dir: Path = args.output_dir
    output_dir.mkdir(parents=True, exist_ok=True)

    print("Reading reviewed sample...")
    df = load_reviewed_sample(args.input_path)

    sample_ids = set(df["hidden_gem_candidate_id"].astype(str).dropna())
    mentions_df, mention_summary = analyze_mentions(args.mentions_path, sample_ids)

    decision_counts_df = count_table(df, "manual_decision", DECISION_ORDER)
    error_counts_df = count_table(df, "manual_error_type", ERROR_TYPE_ORDER)
    tier_quality_df = safe_groupby_quality(df, ["hidden_gem_tier"])
    priority_quality_df = safe_groupby_quality(df, ["manual_review_priority"])
    evidence_quality_df = safe_groupby_quality(df, ["evidence_tier"])
    district_quality_df = safe_groupby_quality(df, ["district_name"])
    dish_quality_df = safe_groupby_quality(df, ["dish_display_name"])

    # Action rows: not necessarily failures, but rows useful for IA v2 planning.
    action_mask = (
        df["manual_decision"].isin(["false_positive", "should_rank_higher", "should_rank_lower", "needs_more_evidence", "doubtful"])
        | (~df["manual_error_type"].isin(["none", "missing_review"]))
        | (df["manual_should_keep_for_v2"].isin(["no", "unclear"]))
    )
    action_candidates = df[action_mask].copy()
    action_candidates = action_candidates.sort_values(
        ["manual_decision", "hidden_gem_score", "mention_count"],
        ascending=[True, False, False],
        na_position="last",
    )
    action_candidates = action_candidates[[c for c in ACTION_COLUMNS if c in action_candidates.columns]]

    false_positive_rows = df[df["manual_decision"] == "false_positive"].copy()
    should_rank_higher_rows = df[df["manual_decision"] == "should_rank_higher"].copy()
    should_rank_lower_rows = df[df["manual_decision"] == "should_rank_lower"].copy()
    needs_more_evidence_rows = df[df["manual_decision"] == "needs_more_evidence"].copy()

    corrected_dish_rows = df[df["manual_corrected_dish_name"].fillna("").astype(str).str.strip() != ""].copy()

    action_plan = build_action_plan(df, mention_summary)
    action_plan_df = pd.DataFrame(action_plan)

    total = len(df)
    correct_count = int((df["manual_decision"] == "correct").sum())
    false_positive_count = int((df["manual_decision"] == "false_positive").sum())
    needs_more_evidence_count = int((df["manual_decision"] == "needs_more_evidence").sum())
    keep_yes_count = int((df["manual_should_keep_for_v2"] == "yes").sum())
    keep_no_count = int((df["manual_should_keep_for_v2"] == "no").sum())
    keep_unclear_count = int((df["manual_should_keep_for_v2"] == "unclear").sum())

    errors = []
    warnings = []
    if total == 0:
        errors.append("No rows in reviewed sample.")
    if df["hidden_gem_candidate_id"].duplicated().any():
        errors.append("Duplicate hidden_gem_candidate_id values in reviewed sample.")
    if (df["manual_decision"] == "missing_review").any():
        warnings.append("Some rows have missing manual_decision.")
    if (df["manual_error_type"] == "missing_review").any():
        warnings.append("Some rows have missing manual_error_type.")
    if mention_summary.get("exact_duplicate_rows", 0) > 0:
        warnings.append("Mention examples contain exact duplicate rows.")

    checks = {
        "has_reviewed_rows": bool(total > 0),
        "candidate_ids_unique": bool(not df["hidden_gem_candidate_id"].duplicated().any()),
        "manual_decision_filled": bool((df["manual_decision"] != "missing_review").all()),
        "manual_error_type_filled": bool((df["manual_error_type"] != "missing_review").all()),
        "has_action_plan": bool(len(action_plan) > 0),
        "mentions_file_checked": bool(mention_summary.get("mentions_path_exists")),
        "mention_examples_link_to_sample": bool(mention_summary.get("linked_to_sample")) if mention_summary.get("linked_to_sample") is not None else None,
    }

    summary = {
        "script": "analyze_sevilla_ai_manual_review_results",
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "input_path": str(args.input_path),
        "mentions_path": str(args.mentions_path),
        "output_dir": str(output_dir),
        "counts": {
            "sample_rows": int(total),
            "places": int(df["place_id"].nunique()) if "place_id" in df.columns else None,
            "dishes": int(df["dish_id"].nunique()) if "dish_id" in df.columns else None,
            "neighborhoods": int(df["neighborhood_id"].nunique()) if "neighborhood_id" in df.columns else None,
            "districts": int(df["district_id"].nunique()) if "district_id" in df.columns else None,
            "selected_rows": int(df["is_selected_bool"].sum()),
            "not_selected_rows": int((~df["is_selected_bool"]).sum()),
        },
        "manual_review": {
            "decision_counts": decision_counts_df.set_index("manual_decision")["count"].to_dict(),
            "error_type_counts": error_counts_df.set_index("manual_error_type")["count"].to_dict(),
            "quality_kpis": {
                "correct_rate": round(correct_count / total * 100, 2) if total else 0.0,
                "false_positive_rate": round(false_positive_count / total * 100, 2) if total else 0.0,
                "needs_more_evidence_rate": round(needs_more_evidence_count / total * 100, 2) if total else 0.0,
                "keep_yes_rate": round(keep_yes_count / total * 100, 2) if total else 0.0,
                "keep_no_rate": round(keep_no_count / total * 100, 2) if total else 0.0,
                "keep_unclear_rate": round(keep_unclear_count / total * 100, 2) if total else 0.0,
            },
            "top_error_types": top_terms(df["manual_error_type"], 8),
            "top_problem_dishes": top_terms(action_candidates.get("dish_display_name", pd.Series(dtype=str)), 10),
            "top_problem_districts": top_terms(action_candidates.get("district_name", pd.Series(dtype=str)), 10),
        },
        "mention_examples": mention_summary,
        "recommended_actions": action_plan,
        "checks": checks,
        "errors": errors,
        "warnings": warnings,
        "files": {
            "summary_json": "manual_review_results_summary.json",
            "decision_summary_csv": "manual_review_decision_summary.csv",
            "error_taxonomy_csv": "manual_review_error_taxonomy_summary.csv",
            "quality_by_tier_csv": "manual_review_quality_by_tier.csv",
            "quality_by_priority_csv": "manual_review_quality_by_priority.csv",
            "quality_by_evidence_csv": "manual_review_quality_by_evidence.csv",
            "quality_by_district_csv": "manual_review_quality_by_district.csv",
            "quality_by_dish_csv": "manual_review_quality_by_dish.csv",
            "action_candidates_csv": "manual_review_action_candidates.csv",
            "false_positives_csv": "manual_review_false_positives.csv",
            "should_rank_higher_csv": "manual_review_should_rank_higher.csv",
            "should_rank_lower_csv": "manual_review_should_rank_lower.csv",
            "needs_more_evidence_csv": "manual_review_needs_more_evidence.csv",
            "corrected_dish_names_csv": "manual_review_corrected_dish_names.csv",
            "ai_v2_action_plan_csv": "manual_review_ai_v2_action_plan.csv",
            "recommendations_md": "recommended_ai_v2_actions.md",
        },
    }

    # Save outputs.
    save_csv(decision_counts_df, output_dir / "manual_review_decision_summary.csv")
    save_csv(error_counts_df, output_dir / "manual_review_error_taxonomy_summary.csv")
    save_csv(tier_quality_df, output_dir / "manual_review_quality_by_tier.csv")
    save_csv(priority_quality_df, output_dir / "manual_review_quality_by_priority.csv")
    save_csv(evidence_quality_df, output_dir / "manual_review_quality_by_evidence.csv")
    save_csv(district_quality_df, output_dir / "manual_review_quality_by_district.csv")
    save_csv(dish_quality_df, output_dir / "manual_review_quality_by_dish.csv")
    save_csv(action_candidates, output_dir / "manual_review_action_candidates.csv")
    save_csv(false_positive_rows[[c for c in ACTION_COLUMNS if c in false_positive_rows.columns]], output_dir / "manual_review_false_positives.csv")
    save_csv(should_rank_higher_rows[[c for c in ACTION_COLUMNS if c in should_rank_higher_rows.columns]], output_dir / "manual_review_should_rank_higher.csv")
    save_csv(should_rank_lower_rows[[c for c in ACTION_COLUMNS if c in should_rank_lower_rows.columns]], output_dir / "manual_review_should_rank_lower.csv")
    save_csv(needs_more_evidence_rows[[c for c in ACTION_COLUMNS if c in needs_more_evidence_rows.columns]], output_dir / "manual_review_needs_more_evidence.csv")
    save_csv(corrected_dish_rows[[c for c in ACTION_COLUMNS if c in corrected_dish_rows.columns]], output_dir / "manual_review_corrected_dish_names.csv")
    save_csv(action_plan_df, output_dir / "manual_review_ai_v2_action_plan.csv")

    md = build_markdown_report(df, summary, action_plan, action_candidates)
    save_md(md, output_dir / "recommended_ai_v2_actions.md")
    save_json(summary, output_dir / "manual_review_results_summary.json")

    print(json.dumps(to_builtin(summary), indent=2, ensure_ascii=False, allow_nan=False))

    if args.strict and errors:
        return 1
    return 0


