    lines.append("2. Improve Spanish dish aliases and composite dish normalization.")
    lines.append("3. Add a stronger evidence/tier policy for top and strong candidates.")
    lines.append("4. Add generic dish controls for broad dishes such as pizza or hamburger.")
    lines.append("5. Recompute Sevilla ranking v2 and compare against v1.")
    lines.append("```")
    lines.append("")

    return "\n".join(lines)


