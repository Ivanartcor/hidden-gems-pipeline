def to_builtin(value: Any) -> Any:
    """Convert pandas/numpy/Decimal/date values into strict JSON-compatible values."""
    if value is None:
        return None

    if isinstance(value, dict):
        return {str(k): to_builtin(v) for k, v in value.items()}

    if isinstance(value, (list, tuple, set)):
        return [to_builtin(v) for v in value]

    if isinstance(value, UUID):
        return str(value)

    if isinstance(value, (datetime, date)):
        return value.isoformat()

    if isinstance(value, Decimal):
        if value.is_nan():
            return None
        return float(value)

    if hasattr(value, "item"):
        try:
            return to_builtin(value.item())
        except Exception:
            pass

    if isinstance(value, float):
        if math.isnan(value) or math.isinf(value):
            return None
        return value

    try:
        if pd.isna(value):
            return None
    except Exception:
        pass

    return value


def save_json(data: dict[str, Any], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        json.dump(to_builtin(data), f, indent=2, ensure_ascii=False, allow_nan=False)


def save_csv(df: pd.DataFrame, path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(path, index=False, encoding="utf-8")


def save_md(text: str, path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def normalize_str_series(series: pd.Series, default: str = "missing_review") -> pd.Series:
    return (
        series.fillna(default)
        .astype(str)
        .str.strip()
        .replace({"": default, "nan": default, "None": default})
    )


def as_bool_like(series: pd.Series) -> pd.Series:
    return series.fillna(False).astype(str).str.lower().isin(["true", "1", "yes", "y", "sí", "si"])


def safe_numeric(df: pd.DataFrame, columns: list[str]) -> pd.DataFrame:
    out = df.copy()
    for col in columns:
        if col in out.columns:
            out[col] = pd.to_numeric(out[col], errors="coerce")
    return out


def count_table(df: pd.DataFrame, column: str, order: list[str] | None = None) -> pd.DataFrame:
    if column not in df.columns:
        return pd.DataFrame(columns=[column, "count", "percentage"])

    counts = df[column].fillna("missing").astype(str).value_counts(dropna=False).reset_index()
    counts.columns = [column, "count"]
    total = counts["count"].sum()
    counts["percentage"] = (counts["count"] / total * 100).round(2) if total else 0.0

    if order:
        order_map = {name: idx for idx, name in enumerate(order)}
        counts["_order"] = counts[column].map(order_map).fillna(len(order_map)).astype(int)
        counts = counts.sort_values(["_order", "count"], ascending=[True, False]).drop(columns="_order")

    return counts.reset_index(drop=True)


def safe_groupby_quality(df: pd.DataFrame, group_cols: list[str]) -> pd.DataFrame:
    available = [c for c in group_cols if c in df.columns]
    if not available or df.empty:
        return pd.DataFrame()

    grouped = (
        df.groupby(available, dropna=False)
        .agg(
            reviewed_rows=("hidden_gem_candidate_id", "count"),
            selected_rows=("is_selected_bool", "sum"),
            correct_count=("manual_decision", lambda s: int((s == "correct").sum())),
            false_positive_count=("manual_decision", lambda s: int((s == "false_positive").sum())),
            doubtful_count=("manual_decision", lambda s: int((s == "doubtful").sum())),
            needs_more_evidence_count=("manual_decision", lambda s: int((s == "needs_more_evidence").sum())),
            should_rank_higher_count=("manual_decision", lambda s: int((s == "should_rank_higher").sum())),
            should_rank_lower_count=("manual_decision", lambda s: int((s == "should_rank_lower").sum())),
            keep_yes_count=("manual_should_keep_for_v2", lambda s: int((s == "yes").sum())),
            keep_no_count=("manual_should_keep_for_v2", lambda s: int((s == "no").sum())),
            keep_unclear_count=("manual_should_keep_for_v2", lambda s: int((s == "unclear").sum())),
            avg_score=("hidden_gem_score", "mean"),
            min_score=("hidden_gem_score", "min"),
            max_score=("hidden_gem_score", "max"),
            avg_mentions=("mention_count", "mean"),
            avg_reviews=("review_count", "mean"),
            avg_positive_ratio=("positive_ratio", "mean"),
            avg_negative_ratio=("negative_ratio", "mean"),
        )
        .reset_index()
    )

    grouped["correct_rate"] = (grouped["correct_count"] / grouped["reviewed_rows"] * 100).round(2)
    grouped["false_positive_rate"] = (grouped["false_positive_count"] / grouped["reviewed_rows"] * 100).round(2)
    grouped["needs_more_evidence_rate"] = (grouped["needs_more_evidence_count"] / grouped["reviewed_rows"] * 100).round(2)
    grouped["keep_yes_rate"] = (grouped["keep_yes_count"] / grouped["reviewed_rows"] * 100).round(2)

    for col in [
        "avg_score",
        "min_score",
        "max_score",
        "avg_mentions",
        "avg_reviews",
        "avg_positive_ratio",
        "avg_negative_ratio",
    ]:
        grouped[col] = grouped[col].round(4)

    return grouped.sort_values(["reviewed_rows", "correct_rate"], ascending=[False, False]).reset_index(drop=True)


def top_terms(series: pd.Series, limit: int = 10) -> list[dict[str, Any]]:
    values = [str(x).strip() for x in series.dropna().tolist() if str(x).strip()]
    return [{"value": k, "count": v} for k, v in Counter(values).most_common(limit)]


