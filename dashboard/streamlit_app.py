def render_candidate_detail(filtered_df: pd.DataFrame, mentions_df: pd.DataFrame) -> None:
    if filtered_df.empty:
        st.info("No hay candidatos disponibles con los filtros actuales.")
        return

    section_title("Detalle de candidato", "Explicabilidad del score, evidencia y reseñas asociadas.")

    labels = [candidate_label(row) for _, row in filtered_df.iterrows()]
    selected_label = st.selectbox("Selecciona un candidato", labels, index=0)
    selected_idx = labels.index(selected_label)
    candidate = filtered_df.iloc[selected_idx]

    st.markdown(
        f"""
        <div class="candidate-card">
            <div>{tier_badge(candidate.get('hidden_gem_tier'))}</div>
            <div class="candidate-title">#{fmt_int(candidate.get('hidden_gem_selected_rank'))} · {candidate.get('dish_display_name', candidate.get('dish_name', 'Plato'))} en {candidate.get('place_name', 'Local')}</div>
            <div class="candidate-subtitle">{candidate.get('neighborhood_name', '')} · {candidate.get('district_name', '')} · {candidate.get('address_text', '')}</div>
            <div class="score-big">{fmt_score(candidate.get('hidden_gem_score'))}/100</div>
            <div class="small-muted">{truncate_text(candidate.get('ranking_explanation'), 300)}</div>
        </div>
        """,
        unsafe_allow_html=True,
    )

    tab_summary, tab_score, tab_reviews = st.tabs(["Resumen", "Componentes del score", "Menciones y reseñas"])

    with tab_summary:
        col1, col2, col3, col4 = st.columns(4)
        with col1:
            st.metric("Menciones", fmt_int(candidate.get("mention_count")))
        with col2:
            st.metric("Reviews", fmt_int(candidate.get("review_count")))
        with col3:
            st.metric("Ratio positivo", fmt_pct(candidate.get("positive_ratio")))
        with col4:
            st.metric("Confianza media", fmt_score(candidate.get("avg_sentiment_confidence")))

        col5, col6 = st.columns(2)
        with col5:
            st.write("**Evidencia**")
            st.write(candidate.get("evidence_tier", "—"))
            st.write("**Calidad agregada**")
            st.write(candidate.get("aggregate_quality_tier", "—"))
        with col6:
            st.write("**Estado de producción**")
            st.write("No producción" if not bool(candidate.get("is_production_ready", False)) else "Producción")
            st.write("**Revisión humana**")
            st.write(candidate.get("human_review_status", "—"))

    with tab_score:
        component_values = []
        for col in DETAIL_COMPONENT_COLUMNS:
            if col in candidate.index and not pd.isna(candidate.get(col)):
                component_values.append({"component": col, "value": float(candidate.get(col))})

        penalty_values = []
        for col in PENALTY_COLUMNS:
            if col in candidate.index and not pd.isna(candidate.get(col)):
                penalty_values.append({"penalty": col, "value": float(candidate.get(col))})

        col1, col2 = st.columns(2)
        with col1:
            st.write("**Componentes positivos / señales**")
            comp_df = pd.DataFrame(component_values)
            if not comp_df.empty:
                plot_bar(comp_df.sort_values("value", ascending=True), "component", "value", "Componentes del score", orientation="h")
            else:
                st.info("No hay componentes disponibles.")

        with col2:
            st.write("**Factores de penalización**")
            pen_df = pd.DataFrame(penalty_values)
            if not pen_df.empty:
                plot_bar(pen_df.sort_values("value", ascending=True), "penalty", "value", "Penalizaciones", orientation="h")
            else:
                st.info("No hay penalizaciones disponibles.")

        display_cols = safe_columns(pd.DataFrame([candidate]), DETAIL_COMPONENT_COLUMNS + PENALTY_COLUMNS)
        if display_cols:
            st.dataframe(pd.DataFrame([candidate])[display_cols], hide_index=True, use_container_width=True)

    with tab_reviews:
        candidate_mentions = get_candidate_mentions(candidate, mentions_df)
        if candidate_mentions.empty:
            st.info("No hay ejemplos de menciones/reseñas exportados para este candidato.")
            st.caption("Vuelve a generar el export con --include-mentions y, si quieres reseñas completas, --include-full-review-text.")
            return

        st.write(f"**Ejemplos exportados:** {len(candidate_mentions)}")

        display_table(
            candidate_mentions,
            [
                "rating_value",
                "sentiment_label",
                "sentiment_score",
                "sentiment_confidence",
                "sentiment_reliability_tier",
                "mention_text",
                "context_sentence",
            ],
            height=260,
        )

        st.markdown("#### Detalle de reseñas")
        for idx, row in candidate_mentions.reset_index(drop=True).iterrows():
            sentiment = row.get("sentiment_label", "—")
            rating = fmt_score(row.get("rating_value"))
            mention = row.get("mention_text", "")
            title = f"Reseña {idx + 1} · rating {rating} · sentimiento {sentiment} · mención: {mention}"
            with st.expander(title):
                st.write("**Contexto de la mención**")
                st.write(row.get("context_sentence", "Sin contexto disponible."))

                if "review_text_raw" in row.index and str(row.get("review_text_raw", "")).strip():
                    st.write("**Reseña completa**")
                    st.write(row.get("review_text_raw"))
                else:
                    st.caption("La reseña completa no está exportada. Genera el dashboard data con --include-full-review-text.")



