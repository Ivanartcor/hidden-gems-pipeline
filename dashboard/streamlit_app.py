def sorted_unique(df: pd.DataFrame, column: str) -> list[str]:
    if df.empty or column not in df.columns:
        return []
    values = df[column].dropna().astype(str).sort_values().unique().tolist()
    return values



def apply_filters(
    df: pd.DataFrame,
    districts: list[str],
    neighborhoods: list[str],
    dishes: list[str],
    tiers: list[str],
    evidence: list[str],
    min_score: float,
    text_search: str,
    only_top_strong: bool,
) -> pd.DataFrame:
    if df.empty:
        return df

    out = df.copy()

    if districts and "district_name" in out.columns:
        out = out[out["district_name"].astype(str).isin(districts)]

    if neighborhoods and "neighborhood_name" in out.columns:
        out = out[out["neighborhood_name"].astype(str).isin(neighborhoods)]

    if dishes and "dish_display_name" in out.columns:
        out = out[out["dish_display_name"].astype(str).isin(dishes)]

    if tiers and "hidden_gem_tier" in out.columns:
        out = out[out["hidden_gem_tier"].astype(str).isin(tiers)]

    if evidence and "evidence_tier" in out.columns:
        out = out[out["evidence_tier"].astype(str).isin(evidence)]

    if "hidden_gem_score" in out.columns:
        out = out[out["hidden_gem_score"].fillna(-1) >= float(min_score)]

    if only_top_strong and "hidden_gem_tier" in out.columns:
        out = out[out["hidden_gem_tier"].isin(["top_hidden_gem", "strong_hidden_gem"])]

    if text_search:
        query = text_search.strip().lower()
        searchable_cols = [
            col
            for col in [
                "place_name",
                "dish_display_name",
                "dish_name",
                "district_name",
                "neighborhood_name",
                "ranking_explanation",
            ]
            if col in out.columns
        ]
        if searchable_cols:
            mask = pd.Series(False, index=out.index)
            for col in searchable_cols:
                mask = mask | out[col].astype(str).str.lower().str.contains(query, na=False)
            out = out[mask]

    return out.sort_values(
        ["hidden_gem_selected_rank", "hidden_gem_score", "mention_count", "review_count"],
        ascending=[True, False, False, False],
        na_position="last",
    )



def candidate_label(row: pd.Series) -> str:
    rank = row.get("hidden_gem_selected_rank", "—")
    rank_txt = fmt_int(rank) if not pd.isna(rank) else "—"
    dish = row.get("dish_display_name", row.get("dish_name", "plato"))
    place = row.get("place_name", "local")
    score = fmt_score(row.get("hidden_gem_score"))
    neighborhood = row.get("neighborhood_name", "")
    return f"#{rank_txt} · {dish} en {place} · {score}/100 · {neighborhood}"



def get_candidate_mentions(candidate: pd.Series, mentions_df: pd.DataFrame) -> pd.DataFrame:
    if mentions_df.empty:
        return mentions_df

    out = mentions_df.copy()

    if "hidden_gem_candidate_id" in out.columns and "hidden_gem_candidate_id" in candidate.index:
        cid = str(candidate.get("hidden_gem_candidate_id"))
        matched = out[out["hidden_gem_candidate_id"].astype(str) == cid]
        if not matched.empty:
            return matched

    place_id = str(candidate.get("place_id", ""))
    dish_id = str(candidate.get("dish_id", ""))
    if "place_id" in out.columns and "dish_id" in out.columns:
        return out[(out["place_id"].astype(str) == place_id) & (out["dish_id"].astype(str) == dish_id)]

    return pd.DataFrame()


