def main() -> None:
    with st.sidebar:
        st.markdown("## ⚙️ Configuración")
        data_dir_input = st.text_input("Carpeta de datos del dashboard", value=str(DEFAULT_DATA_DIR))
        data_dir = as_path(data_dir_input)

        if st.button("Recargar datos"):
            st.cache_data.clear()
            st.rerun()

        st.caption("Los datos deben generarse previamente con `scripts.export_sevilla_dashboard_data`.")

    if not data_dir.exists():
        st.error(f"No existe la carpeta de datos: {data_dir}")
        st.stop()

    data = load_dashboard_data(str(data_dir))
    candidates_detail = data["candidates_detail"]
    candidates_all = data["candidates_all"]

    if candidates_detail.empty:
        st.error("No se encontró `candidates_detail.csv` o está vacío. Ejecuta primero el exportador del dashboard.")
        st.stop()

    metadata = data["metadata"]
    kpis = data["kpis"]

    render_header(metadata, kpis)

    with st.sidebar:
        st.markdown("## 🔎 Filtros")
        use_all = st.checkbox("Incluir candidatos no seleccionados", value=False)
        base_df = candidates_all if use_all and not candidates_all.empty else candidates_detail

        district_options = sorted_unique(base_df, "district_name")
        selected_districts = st.multiselect("Distrito", district_options)

        neighborhood_options_df = base_df
        if selected_districts and "district_name" in neighborhood_options_df.columns:
            neighborhood_options_df = neighborhood_options_df[neighborhood_options_df["district_name"].astype(str).isin(selected_districts)]
        neighborhood_options = sorted_unique(neighborhood_options_df, "neighborhood_name")
        selected_neighborhoods = st.multiselect("Barrio", neighborhood_options)

        dish_options = sorted_unique(base_df, "dish_display_name")
        selected_dishes = st.multiselect("Plato", dish_options)

        tier_options = [tier for tier in TIER_ORDER if tier in sorted_unique(base_df, "hidden_gem_tier")]
        selected_tiers = st.multiselect("Tier", tier_options, format_func=tier_label)

        evidence_options = sorted_unique(base_df, "evidence_tier")
        selected_evidence = st.multiselect("Evidencia", evidence_options)

        score_min_available = float(base_df["hidden_gem_score"].min()) if "hidden_gem_score" in base_df.columns else 0.0
        score_max_available = float(base_df["hidden_gem_score"].max()) if "hidden_gem_score" in base_df.columns else 100.0

        if math.isnan(score_min_available) or math.isinf(score_min_available):
            score_min_available = 0.0
        if math.isnan(score_max_available) or math.isinf(score_max_available):
            score_max_available = 100.0

        default_min_score = float(max(0.0, math.floor(score_min_available)))

        min_score = st.slider(
            "Score mínimo",
            min_value=0.0,
            max_value=100.0,
            value=default_min_score,
            step=1.0,
        )

        only_top_strong = st.checkbox("Mostrar solo Top + Strong", value=False)
        text_search = st.text_input("Buscar texto", placeholder="local, plato, barrio...")

    base_df = candidates_all if use_all and not candidates_all.empty else candidates_detail
    filtered_df = apply_filters(
        base_df,
        districts=selected_districts,
        neighborhoods=selected_neighborhoods,
        dishes=selected_dishes,
        tiers=selected_tiers,
        evidence=selected_evidence,
        min_score=min_score,
        text_search=text_search,
        only_top_strong=only_top_strong,
    )

    render_kpi_cards(kpis)

    st.markdown("---")
    tabs = st.tabs(
        [
            "🏠 Resumen",
            "🔎 Explorador",
            "🗺️ Territorio",
            "🍽️ Platos y locales",
            "🧾 Detalle",
            "✅ Calidad",
        ]
    )

    with tabs[0]:
        render_overview(data, filtered_df)

    with tabs[1]:
        render_explorer(filtered_df)

    with tabs[2]:
        render_territory(data, filtered_df)

    with tabs[3]:
        render_dishes_places(data)

    with tabs[4]:
        render_candidate_detail(filtered_df, data["mention_examples"])

    with tabs[5]:
        render_quality(data)


if __name__ == "__main__":
    main()
