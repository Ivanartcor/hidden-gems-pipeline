def safe_columns(df: pd.DataFrame, columns: list[str]) -> list[str]:
    return [col for col in columns if col in df.columns]



def display_table(df: pd.DataFrame, columns: list[str] | None = None, height: int = 420) -> None:
    if df.empty:
        st.info("No hay datos para mostrar con los filtros actuales.")
        return

    view = df[safe_columns(df, columns)] if columns else df
    st.dataframe(view, use_container_width=True, height=height, hide_index=True)



def plot_bar(
    df: pd.DataFrame,
    x: str,
    y: str,
    title: str | None = None,
    color: str | None = None,
    orientation: str = "v",
    height: int = 360,
) -> None:
    if df.empty or x not in df.columns or y not in df.columns:
        st.info("No hay datos suficientes para la gráfica.")
        return

    if HAS_PLOTLY:
        if orientation == "h":
            fig = px.bar(df, x=y, y=x, color=color if color in df.columns else None, orientation="h", title=title)
            fig.update_layout(yaxis={"categoryorder": "total ascending"}, height=height, margin=dict(l=10, r=10, t=50, b=10))
        else:
            fig = px.bar(df, x=x, y=y, color=color if color in df.columns else None, title=title)
            fig.update_layout(height=height, margin=dict(l=10, r=10, t=50, b=10))
        st.plotly_chart(fig, use_container_width=True)
    else:
        st.caption(title or "")
        chart_df = df[[x, y]].set_index(x)
        st.bar_chart(chart_df, height=height)



def plot_scatter_map(df: pd.DataFrame) -> None:
    if df.empty or "latitude" not in df.columns or "longitude" not in df.columns:
        st.info("No hay coordenadas disponibles para el mapa.")
        return

    map_df = df.dropna(subset=["latitude", "longitude"]).copy()
    if map_df.empty:
        st.info("No hay coordenadas válidas para el mapa.")
        return

    if HAS_PLOTLY:
        hover_cols = [c for c in ["place_name", "dish_display_name", "hidden_gem_score", "neighborhood_name", "district_name"] if c in map_df.columns]
        fig = px.scatter_mapbox(
            map_df,
            lat="latitude",
            lon="longitude",
            color="hidden_gem_tier" if "hidden_gem_tier" in map_df.columns else None,
            size="hidden_gem_score" if "hidden_gem_score" in map_df.columns else None,
            hover_name="place_name" if "place_name" in map_df.columns else None,
            hover_data=hover_cols,
            zoom=11,
            height=520,
        )
        fig.update_layout(mapbox_style="open-street-map", margin=dict(l=0, r=0, t=0, b=0))
        st.plotly_chart(fig, use_container_width=True)
    else:
        st.map(map_df.rename(columns={"latitude": "lat", "longitude": "lon"})[["lat", "lon"]])



