def main() -> None:
    with st.sidebar:
        st.header("⚙️ Configuración")
        data_dir_input = st.text_input("Carpeta de datos", value=str(DEFAULT_DATA_DIR))
        data_dir = normalize_path(data_dir_input)

        st.caption("Genera estos datos con `scripts/export_yelp_dashboard_data.py`.")

    data = load_dashboard_data(data_dir)
    metadata = data["metadata"]
    kpis = data["kpis"]
    export_summary = data["export_summary"]
    data_contract = data["data_contract"]

    candidates_all: pd.DataFrame = data["candidates_all"]
    candidates_detail: pd.DataFrame = data["candidates_detail"]
    top_global_file: pd.DataFrame = data["top_global"]
    city_summary: pd.DataFrame = data["city_summary"]
    state_summary: pd.DataFrame = data["state_summary"]
    dish_summary: pd.DataFrame = data["dish_summary"]
    place_summary: pd.DataFrame = data["place_summary"]
    tier_summary: pd.DataFrame = data["tier_summary"]
    quality_summary: pd.DataFrame = data["quality_summary"]
    mention_examples: pd.DataFrame = data["mention_examples"]

    if candidates_all.empty and candidates_detail.empty:
        st.error(
            "No se encontraron candidates_all.csv ni candidates_detail.csv. "
            "Ejecuta primero scripts/export_yelp_dashboard_data.py."
        )
        st.code(
            "python -m scripts.export_yelp_dashboard_data `\n"
            "  --output-dir data/artifacts/ai/yelp/dashboard `\n"
            "  --expected-selected 622 `\n"
            "  --include-mentions `\n"
            "  --strict"
        )
        return

    base_df = candidates_detail if not candidates_detail.empty else candidates_all
    selected_df = base_df[base_df.get("is_selected", True) == True].copy() if "is_selected" in base_df.columns else base_df.copy()

    render_header(metadata, export_summary)
    render_kpis(kpis)

    # Sidebar filters after data load
    with st.sidebar:
        st.divider()
        st.header("🔎 Filtros")
        include_all = st.checkbox("Incluir no seleccionados", value=False, help="Yelp prototype normalmente tiene 622 seleccionados.")
        working_df = candidates_all.copy() if include_all and not candidates_all.empty else selected_df.copy()

        state_options = sorted([x for x in working_df.get("inferred_state", pd.Series(dtype=str)).dropna().astype(str).unique() if x])
        selected_states = st.multiselect("Estado", state_options, default=[])

        city_source = working_df
        if selected_states and "inferred_state" in city_source.columns:
            city_source = city_source[city_source["inferred_state"].isin(selected_states)]
        city_options = sorted([x for x in city_source.get("inferred_city", pd.Series(dtype=str)).dropna().astype(str).unique() if x])
        selected_cities = st.multiselect("Ciudad", city_options, default=[])

        dish_options = sorted([x for x in working_df.get("dish_dashboard_name", pd.Series(dtype=str)).dropna().astype(str).unique() if x])
        selected_dishes = st.multiselect("Plato", dish_options, default=[])

        tier_options = [t for t in TIER_ORDER.keys() if t in set(working_df.get("hidden_gem_tier", pd.Series(dtype=str)).astype(str))]
        selected_tiers = st.multiselect("Tier", tier_options, default=[])

        evidence_options = sorted([x for x in working_df.get("evidence_tier", pd.Series(dtype=str)).dropna().astype(str).unique() if x])
        selected_evidence = st.multiselect("Evidencia", evidence_options, default=[])

        score_series = working_df.get("hidden_gem_score", pd.Series([0.0]))
        score_min_available = safe_float(score_series.min(), 0.0)
        default_min_score = float(max(0.0, math.floor(score_min_available)))
        min_score = st.slider(
            "Score mínimo",
            min_value=0.0,
            max_value=100.0,
            value=default_min_score,
            step=1.0,
        )

        only_top_strong = st.checkbox("Solo Top + Strong", value=False)
        search_query = st.text_input("Búsqueda libre", value="", placeholder="local, plato, ciudad, estado...")

        st.divider()
        st.caption("Este dashboard no consulta PostgreSQL directamente; lee CSV/JSON exportados.")

    working_df = candidates_all.copy() if include_all and not candidates_all.empty else selected_df.copy()
    filtered = apply_filters(
        working_df,
        states=selected_states,
        cities=selected_cities,
        dishes=selected_dishes,
        tiers=selected_tiers,
        evidence=selected_evidence,
        min_score=min_score,
        only_top_strong=only_top_strong,
        query=search_query,
    )

    st.markdown("---")
    fcols = st.columns(5)
    fcols[0].metric("Filtrados", format_number(len(filtered)))
    fcols[1].metric("Locales", format_number(filtered["place_id"].nunique() if "place_id" in filtered.columns else 0))
    fcols[2].metric("Platos", format_number(filtered["dish_id"].nunique() if "dish_id" in filtered.columns else 0))
    fcols[3].metric("Ciudades", format_number(filtered["inferred_city"].nunique() if "inferred_city" in filtered.columns else 0))
    fcols[4].metric("Estados", format_number(filtered["inferred_state"].nunique() if "inferred_state" in filtered.columns else 0))

    tab_overview, tab_ranking, tab_geo, tab_dish, tab_place, tab_detail, tab_quality = st.tabs(
        [
            "📌 Resumen",
            "🏆 Ranking",
            "🌎 Ciudades/Estados",
            "🍽️ Platos",
            "🏪 Locales",
            "🔍 Detalle",
            "✅ Calidad",
        ]
    )

    with tab_overview:
        st.subheader("Resumen del benchmark IA")
        st.markdown(
            """
            Este panel muestra la validación a gran escala del flujo IA de Hidden Gems sobre Yelp:
            detección de platos, sentimiento por mención, agregación local-plato y ranking explicable.
            """
        )
        col1, col2 = st.columns([1, 1])
        with col1:
            render_pie_or_bar_tiers(tier_summary, selected_df, chart_key="quality_tier_distribution")
        with col2:
            city_col = first_existing(city_summary, ["inferred_city", "city", "city_name"])
            count_col = first_existing(city_summary, ["selected_count", "candidate_count", "count"])
            if city_col and count_col:
                render_bar_chart(city_summary, city_col, count_col, "Ciudades con más candidatos", top_n=15, chart_key="quality_top_cities")
            else:
                st.dataframe(city_summary, use_container_width=True)

        st.markdown("### Top 10 global")
        render_top_cards(filtered, max_cards=10)

    with tab_ranking:
        render_ranking_tab(filtered, selected_df, tier_summary)

    with tab_geo:
        render_geo_tab(filtered, city_summary, state_summary)

    with tab_dish:
        render_dish_tab(filtered, dish_summary)

    with tab_place:
        render_place_tab(filtered, place_summary)

    with tab_detail:
        render_detail_tab(filtered, mention_examples)

    with tab_quality:
        render_quality_tab(metadata, export_summary, quality_summary, data_contract)


if __name__ == "__main__":
    main()
