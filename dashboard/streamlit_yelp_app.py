def normalize_path(value: str | Path) -> Path:
    return Path(str(value).strip()).expanduser()


@st.cache_data(show_spinner=False)
def load_json(path: str) -> dict[str, Any]:
    p = Path(path)
    if not p.exists():
        return {}
    try:
        return json.loads(p.read_text(encoding="utf-8"))
    except Exception:
        return {}


@st.cache_data(show_spinner=False)
def load_csv(path: str) -> pd.DataFrame:
    p = Path(path)
    if not p.exists():
        return pd.DataFrame()
    df = pd.read_csv(p)
    return normalize_dataframe(df)



def normalize_dataframe(df: pd.DataFrame) -> pd.DataFrame:
    if df.empty:
        return df
    out = df.copy()

    for col in NUMERIC_COLUMNS:
        if col in out.columns:
            out[col] = pd.to_numeric(out[col], errors="coerce")

    for col in BOOL_COLUMNS:
        if col in out.columns:
            if out[col].dtype == bool:
                out[col] = out[col].fillna(False)
            else:
                out[col] = out[col].astype(str).str.lower().isin(["true", "1", "t", "yes", "y"])

    if "dish_dashboard_name" not in out.columns:
        if "dish_display_name" in out.columns:
            out["dish_dashboard_name"] = out["dish_display_name"].fillna(out.get("dish_name", ""))
        elif "dish_name" in out.columns:
            out["dish_dashboard_name"] = out["dish_name"]
        else:
            out["dish_dashboard_name"] = ""

    if "inferred_city" not in out.columns:
        out["inferred_city"] = out.get("city", "")
    if "inferred_state" not in out.columns:
        out["inferred_state"] = out.get("state", "")

    if "hidden_gem_tier_order" not in out.columns and "hidden_gem_tier" in out.columns:
        out["hidden_gem_tier_order"] = out["hidden_gem_tier"].map(TIER_ORDER).fillna(0).astype(int)

    return out



