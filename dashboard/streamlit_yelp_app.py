def apply_filters(
    df: pd.DataFrame,
    states: list[str],
    cities: list[str],
    dishes: list[str],
    tiers: list[str],
    evidence: list[str],
    min_score: float,
    only_top_strong: bool,
    query: str,
) -> pd.DataFrame:
    if df.empty:
        return df
    out = df.copy()

    if states and "inferred_state" in out.columns:
        out = out[out["inferred_state"].isin(states)]
    if cities and "inferred_city" in out.columns:
        out = out[out["inferred_city"].isin(cities)]
    if dishes and "dish_dashboard_name" in out.columns:
        out = out[out["dish_dashboard_name"].isin(dishes)]
    if tiers and "hidden_gem_tier" in out.columns:
        out = out[out["hidden_gem_tier"].isin(tiers)]
    if evidence and "evidence_tier" in out.columns:
        out = out[out["evidence_tier"].isin(evidence)]
    if "hidden_gem_score" in out.columns:
        out = out[out["hidden_gem_score"].fillna(0) >= float(min_score)]
    if only_top_strong and "hidden_gem_tier" in out.columns:
        out = out[out["hidden_gem_tier"].isin(["top_hidden_gem", "strong_hidden_gem"])]

    if query:
        q = query.strip().lower()
        if q:
            search_cols = [
                c
                for c in ["place_name", "dish_dashboard_name", "dish_name", "inferred_city", "inferred_state", "address_text"]
                if c in out.columns
            ]
            if search_cols:
                mask = pd.Series(False, index=out.index)
                for col in search_cols:
                    mask = mask | out[col].astype(str).str.lower().str.contains(q, na=False)
                out = out[mask]

    sort_cols = [c for c in ["hidden_gem_selected_rank", "hidden_gem_score", "mention_count", "review_count"] if c in out.columns]
    if sort_cols:
        out = out.sort_values(sort_cols, ascending=[True] + [False] * (len(sort_cols) - 1), na_position="last")
    return out.reset_index(drop=True)


