def get_candidate_mentions(mentions_df: pd.DataFrame, candidate_id: str, place_id: str, dish_id: str) -> pd.DataFrame:
    if mentions_df.empty:
        return pd.DataFrame()
    if "hidden_gem_candidate_id" in mentions_df.columns:
        out = mentions_df[mentions_df["hidden_gem_candidate_id"].astype(str) == str(candidate_id)].copy()
        if not out.empty:
            return out
    place_mask = mentions_df.get("place_id", pd.Series(dtype=str)).astype(str) == str(place_id)
    dish_mask = mentions_df.get("dish_id", pd.Series(dtype=str)).astype(str) == str(dish_id)
    return mentions_df[place_mask & dish_mask].copy()


