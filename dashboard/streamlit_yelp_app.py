def safe_int(value: Any, default: int = 0) -> int:
    try:
        if pd.isna(value):
            return default
        return int(value)
    except Exception:
        return default



def safe_float(value: Any, default: float = 0.0) -> float:
    try:
        if pd.isna(value):
            return default
        return float(value)
    except Exception:
        return default



def format_number(value: Any, decimals: int = 0) -> str:
    num = safe_float(value, 0.0)
    if decimals == 0:
        return f"{num:,.0f}".replace(",", ".")
    return f"{num:,.{decimals}f}".replace(",", "X").replace(".", ",").replace("X", ".")



def pct(value: Any) -> str:
    return f"{safe_float(value, 0.0) * 100:.1f}%"



def get_kpi(kpis: dict[str, Any], key: str, fallback: Any = 0) -> Any:
    if key in kpis:
        return kpis[key]
    nested = kpis.get("kpis")
    if isinstance(nested, dict):
        return nested.get(key, fallback)
    return fallback



def first_existing(df: pd.DataFrame, candidates: Iterable[str]) -> str | None:
    for col in candidates:
        if col in df.columns:
            return col
    return None



def subset_columns(df: pd.DataFrame, columns: list[str]) -> list[str]:
    return [c for c in columns if c in df.columns]



def shorten(text: Any, max_len: int = 160) -> str:
    if text is None or (isinstance(text, float) and math.isnan(text)):
        return ""
    s = str(text).replace("\n", " ").replace("\r", " ").strip()
    if len(s) <= max_len:
        return s
    return s[: max_len - 1] + "…"



