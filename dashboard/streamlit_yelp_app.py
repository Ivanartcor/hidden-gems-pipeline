def render_detail_tab(filtered: pd.DataFrame, mention_examples: pd.DataFrame) -> None:
    st.subheader("Detalle de candidato")
    if filtered.empty:
        st.info("No hay candidatos con los filtros actuales.")
        return

    options_df = filtered.copy().reset_index(drop=True)
    labels = [candidate_option_label(row) for _, row in options_df.iterrows()]
    selected_label = st.selectbox("Selecciona un candidato", labels, index=0)
    selected_idx = labels.index(selected_label)
    row = options_df.iloc[selected_idx]

    dish = row.get("dish_dashboard_name") or row.get("dish_display_name") or row.get("dish_name") or "Plato"
    place = row.get("place_name", "Local")
    city = row.get("inferred_city", "")
    state = row.get("inferred_state", "")
    score = safe_float(row.get("hidden_gem_score"), 0.0)
    tier = str(row.get("hidden_gem_tier", ""))

    st.markdown(
        f"""
        <div class="candidate-card">
            <div class="small-muted">{city}, {state}</div>
            <h2 style="margin:0.1rem 0 0.3rem 0;">{dish} en {place}</h2>
            {tier_badge_html(tier)}
            <span class="score-badge">{score:.1f}/100</span>
            <p style="margin-top:0.75rem;">{row.get("ranking_explanation", "")}</p>
        </div>
        """,
        unsafe_allow_html=True,
    )

    cols = st.columns(5)
    cols[0].metric("Menciones", format_number(row.get("mention_count")))
    cols[1].metric("Reviews", format_number(row.get("review_count")))
    cols[2].metric("Positivas", pct(row.get("positive_ratio")))
    cols[3].metric("Negativas", pct(row.get("negative_ratio")))
    cols[4].metric("Confianza", pct(row.get("avg_sentiment_confidence")))

    st.markdown("### Componentes del score")
    component_cols = [c for c in DETAIL_COMPONENT_COLUMNS if c in row.index]
    if component_cols:
        comp_df = pd.DataFrame(
            {"component": component_cols, "value": [safe_float(row.get(c), 0.0) for c in component_cols]}
        )
        if HAS_PLOTLY:
            fig = px.bar(comp_df, x="component", y="value", text="value", title="Componentes normalizados")
            fig.update_traces(texttemplate="%{text:.2f}")
            fig.update_layout(height=360, xaxis_title="", yaxis_title="")
            fig.update_xaxes(tickangle=-30)
            st.plotly_chart(fig, use_container_width=True, key="candidate_score_components")
        else:
            st.bar_chart(comp_df.set_index("component")["value"])

    st.markdown("### Penalizaciones")
    penalty_cols = [c for c in PENALTY_COLUMNS if c in row.index]
    if penalty_cols:
        penalty_df = pd.DataFrame(
            {"penalty": penalty_cols, "factor": [safe_float(row.get(c), 0.0) for c in penalty_cols]}
        )
        st.dataframe(penalty_df, use_container_width=True, hide_index=True)

    st.markdown("### Datos técnicos")
    technical_cols = subset_columns(
        pd.DataFrame([row]),
        [
            "hidden_gem_candidate_id",
            "place_id",
            "dish_id",
            "dish_place_signal_id",
            "ranking_version",
            "ranking_scope",
            "aggregate_quality_tier",
            "evidence_tier",
            "avg_ner_confidence",
            "bayesian_sentiment_score",
            "total_signal_weight",
            "avg_rating",
            "human_review_status",
            "is_production_ready",
        ],
    )
    st.dataframe(pd.DataFrame([row])[technical_cols].T.rename(columns={0: "value"}), use_container_width=True)

    st.markdown("### Menciones y reseñas asociadas")
    candidate_mentions = get_candidate_mentions(
        mention_examples,
        candidate_id=str(row.get("hidden_gem_candidate_id", "")),
        place_id=str(row.get("place_id", "")),
        dish_id=str(row.get("dish_id", "")),
    )
    if candidate_mentions.empty:
        st.info("No hay menciones exportadas para este candidato. Regenera con --include-mentions si quieres ver ejemplos.")
        return

    mention_cols = subset_columns(
        candidate_mentions,
        [
            "rating_value",
            "review_language",
            "review_created_at",
            "mention_text",
            "sentiment_label",
            "sentiment_score",
            "sentiment_confidence",
            "sentiment_reliability_tier",
            "sentiment_reason",
            "target_clause_context",
            "near_mention_context",
            "context_sentence",
        ],
    )
    compact = candidate_mentions[mention_cols].copy()
    for text_col in ["target_clause_context", "near_mention_context", "context_sentence"]:
        if text_col in compact.columns:
            compact[text_col] = compact[text_col].map(lambda x: shorten(x, 220))
    st.dataframe(compact, use_container_width=True, height=360)

    if "review_text_raw" in candidate_mentions.columns:
        st.markdown("#### Reseñas completas")
        st.caption("Las reseñas completas se muestran solo bajo demanda. No subas este CSV a Git si contiene texto completo.")
        for i, (_, mention_row) in enumerate(candidate_mentions.iterrows(), start=1):
            label = (
                f"Review {i} · rating {mention_row.get('rating_value', '')} · "
                f"{mention_row.get('sentiment_label', '')} · confianza {safe_float(mention_row.get('sentiment_confidence'), 0.0):.2f}"
            )
            with st.expander(label):
                st.write(mention_row.get("review_text_raw", ""))



def render_quality_tab(
    metadata: dict[str, Any],
    export_summary: dict[str, Any],
    quality_summary: pd.DataFrame,
    data_contract: dict[str, Any],
) -> None:
    st.subheader("Calidad, checks y limitaciones")

    checks = export_summary.get("checks", {}) if isinstance(export_summary, dict) else {}
    if checks:
        check_rows = [
            {"check": key, "status": bool(value), "value": value}
            for key, value in checks.items()
        ]
        check_df = pd.DataFrame(check_rows)
        st.dataframe(check_df, use_container_width=True, hide_index=True)
    elif not quality_summary.empty:
        st.dataframe(quality_summary, use_container_width=True)
    else:
        st.info("No se encontró quality_summary ni checks en dashboard_export_summary.json.")

    st.markdown("### Limitaciones del prototipo Yelp")
    st.markdown(
        """
        - Yelp se usa como **benchmark/prototipo IA**, no como fuente productiva de Sevilla.
        - El ranking usa `ranking_scope = yelp_prototype` y `is_production_ready = false`.
        - La geografía disponible es ciudad/estado, no barrios de Sevilla.
        - El corpus principal está en inglés y puede tener sesgos geográficos del dataset Yelp.
        - Las reseñas completas solo deben usarse para revisión local o académica, no para publicar artefactos pesados.
        """
    )

    with st.expander("Metadata"):
        st.json(metadata)

    with st.expander("Dashboard export summary"):
        st.json(export_summary)

    with st.expander("Data contract"):
        st.json(data_contract)


