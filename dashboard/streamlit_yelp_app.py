def render_bar_chart(
    df: pd.DataFrame,
    x: str,
    y: str,
    title: str,
    color: str | None = None,
    top_n: int = 25,
    chart_key: str | None = None,
) -> None:
    if df.empty or x not in df.columns or y not in df.columns:
        st.info("No hay datos suficientes para esta gráfica.")
        return

    plot_df = df[[x, y] + ([color] if color and color in df.columns else [])].dropna(subset=[x, y]).copy()
    plot_df = plot_df.sort_values(y, ascending=False).head(top_n)
    if plot_df.empty:
        st.info("No hay datos suficientes para esta gráfica.")
        return

    if HAS_PLOTLY:
        fig = px.bar(
            plot_df,
            x=x,
            y=y,
            color=color if color in plot_df.columns else None,
            title=title,
            text=y,
        )
        fig.update_layout(height=440, xaxis_title="", yaxis_title="", showlegend=bool(color and color in plot_df.columns))
        fig.update_xaxes(tickangle=-35)
        st.plotly_chart(fig, use_container_width=True, key=chart_key or f"bar_{x}_{y}_{title}")
    else:
        st.bar_chart(plot_df.set_index(x)[y])



def render_pie_or_bar_tiers(
    tier_df: pd.DataFrame,
    candidates_df: pd.DataFrame,
    chart_key: str = "tier_distribution_chart",
) -> None:
    if tier_df.empty and candidates_df.empty:
        st.info("No hay datos de tiers.")
        return

    if tier_df.empty:
        tier_df = (
            candidates_df.groupby("hidden_gem_tier", dropna=False)
            .agg(candidate_count=("hidden_gem_candidate_id", "count"), avg_score=("hidden_gem_score", "mean"))
            .reset_index()
        )
    count_col = first_existing(tier_df, ["candidate_count", "selected_count", "count"])
    if count_col is None or "hidden_gem_tier" not in tier_df.columns:
        st.dataframe(tier_df, use_container_width=True)
        return

    tier_df = tier_df.copy()
    tier_df["tier_label"] = tier_df["hidden_gem_tier"].map(tier_label)
    tier_df["tier_order"] = tier_df["hidden_gem_tier"].map(TIER_ORDER).fillna(0)
    tier_df = tier_df.sort_values("tier_order", ascending=False)

    if HAS_PLOTLY:
        fig = px.pie(
            tier_df,
            names="tier_label",
            values=count_col,
            title="Distribución por tier del ranking Yelp prototype",
            hole=0.45,
        )
        fig.update_layout(height=430)
        st.plotly_chart(fig, use_container_width=True, key=chart_key)
    else:
        st.bar_chart(tier_df.set_index("tier_label")[count_col])



